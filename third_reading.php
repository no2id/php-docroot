<h1> Government suppresses debate, and Tories withdraw support </h1>
<p>In a tense third reading of the ID Cards Bill, MPs of all parties tore holes
				in the proposed legislation and delivered sustained attacks on
		the Government&rsquo;s
		behaviour.</p>
<p>But with less than 45% of MPs voting, the Bill was passed despite 36 cross-party
		rebels joining the Liberal Democrats and Nationalists in opposition. The Conservative&rsquo;s
		withdrawal of support meant that despite the highly vocal criticism from their
		front bench, almost the entire party abstained.</p>
<p>&ldquo;I remind the House that we are elected representatives. I am not a
		robot, a sheep or a button to be pressed&rdquo;, said John Bercow MP, voicing
		outrage at the government&rsquo;s contempt for Parliamentary scrutiny.</p>
<p><a name="more"></a>The Government has &ldquo;steamrollered&rdquo; over the concerns of MPs and
		both the Home Affairs Select Committee and Joint Committee on Human Rights.
		The timetable for the Standing Committee (where the Bill is supposed to undergo
		proper scrutiny) was drastically cut to just 4 days within a fortnight, leaving
		over half a dozen clauses completely unexamined. And Parliamentary debate was
		guillotined to a few hours on a Thursday afternoon, already noted by the Speaker
		as &ldquo;unsuited for major legislation&rdquo;.</p>
<p>The Home Office response to the Joint Committee on Human Rights was returned
		late, on the day of the vote &ndash; so late that most MPs were unable to read
		it. If they had, they would see that it is filled only with a cut-and-paste
		of the same lip service to the important issues of liberty, privacy, and protection
		from the state that has been seen in previous Government replies.</p>
<p>David Davis MP, in a withering speech, surmised: &ldquo;ID cards involve a
		completely new view of citizenship. Being a subject of the Crown is a right,
		which it is not for the Government to give or take away&hellip; The Government
		have denied debate and treated this House with contempt.&rdquo;</p>
<p>And at the third reading, even the Home Secretary Charles Clarke could not
		bring himself to repeat some of the unfounded benefits of ID cards: &ldquo;There
		is no guarantee that any scheme will be able to deal with and eliminate the
		terrorist threat. There is no silver bullet to solve the problem.&rdquo;</p>
<p>But this is exactly what the Government has sold the British public &ndash; a
		magic bullet that will protect us from terrorism, end our immigration worries
		and keep crime off the streets. And incidentally, an idea to placate the voters
		on the eve of an election whose themes will be security, immigration and crime.</p>
<p>The Government&rsquo;s claims for ID cards remain unsubstantiated, and the
		supposed &lsquo;benefits&rsquo; simply don&rsquo;t add up &ndash; especially
		when measured against the spiralling cost of the scheme - whereas the implications
		that each law-abiding member of the country be tagged and tracked by police
		with powers to stop and scan are all too real. The Lords may need little persuading
		to throw out such illiberal and arbitrary legislation.</p>
<p>Those politicians who failed to vote to protect our liberties and rights as
		democratic citizens can be sure that NO2ID and our many thousands of supporters
		across the UK will make sure they are held to account when we next come to
		vote for them!</p>

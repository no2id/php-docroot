<h1>Milton Keynes Council, Second Motion</h1>
<h2>September, 2006</h2>

<p>Following the <a href="/resources/motions/mk1.doc">previous 
   motion</a>, Milton Keynes council have passed this motion:</p>

<p>This Council:</p>
<ol>
  <li>Notes that on the 14 June 2005 a motion was passed declaring
      this Council's opposition to a National Identity Card and 
      Database scheme, to write to and enlist the support of our 
      MPs to request the government to spend the money allocated 
      for this scheme into local policing.</li>

  <li>Regrets that despite overwhelming public opposition the Labour
      government has continued its pursuit of such an Orwellian 
      scheme and re-affirms its request that the funds being made 
      available to this scheme should be spent supporting local 
      policing.</li>

  <li>Will assist the local NO2ID campaign in educating the 
      residents of Milton Keynes about the dangers to privacy and 
      freedom the scheme will create.</li>

  <li>Invites the Cabinet to resist the new Identity Card scheme 
      where legally possible by:</li>
      
  <li>
   <ol style="list-style-type: lower-alpha">
     <li>refusing to take part in any pilot scheme; and</li>
     <li>accepting alternative and traditional methods of 
         identification from residents wishing to access council 
	 services and benefits</li>
   </ol>
  </li>
</ol>


<h1>Councils, unions and the nations against ID</h1>

<p>A growing number of Councils and other major bodies, such as the Scottish
Parliament, and the Welsh and London Assemblies have passed strongly worded
motions against ID cards. Many of these will refuse to cooperate with the ID
scheme; some have even affiliated to NO2ID.</p>

<p>We would encourage you, even if there is not a local group in your area, to
lobby your local Councillors to pass a motion against ID cards. You can do
this by making an appointment or writing a letter.</p>

<p>NO2ID has produced a briefing document for Councillors about the impact
of the National Identity Scheme on local government, which you can
<a href="/resources/councillorsBriefing.doc">download</a> (MS Word document).</p>

<p>Every Council is different. Don't forget to speak to Councillors of all
parties, not just the one(s) who propose the motion. Lay things on the line
&mdash; the millions that each Local Authority is going to have to pay to make its
systems compliant with the ID Register and people's ID number usually wakes
people up. This money has to come from somewhere &mdash; either cuts in local
services, or rises in Council Tax.</p>

<p>It is also worth pointing out that the Home Office's stated position is
currently that every Government Department, Local Authority and public
sector body will have to do a "business case" and decide whether or not to
'buy in' to the ID scheme, i.e. all the costs will come out of their
existing budgets, but there are NO PENALTIES (yet) for those that decide not
to join up.</p>

<p>NO2ID is all-party, non-partisan in our approach so do try to act as an
'honest broker' and make sure that everyone is as happy with the draft
motion as possible before the meeting. Do not make assumptions about who
will or won't support the motion &mdash; ask. You may find Labour Councillors
abstaining or even voting for the motion in some cases, as the ID scheme is
far from popular within the wider Labour movement.</p>

<p>Why should a Council affiliate to NO2ID? Other than becoming part of the
rapidly growing movement, they will (a) get a vote within the organisation
and be able to propose people to our Advisory Board, (b) have access to help
and information from NO2ID, e.g. if they need to work out the impact of the
scheme in their Local Authority, and (c) establish a strongly pro-civil
liberties and privacy stance to their constituents. Refusing to comply with
ID cards could be seen purely as a concern about cost. Affiliating to NO2ID
makes it clear that this is a principled action.</p>

<p>Don't give it up without a fight, but don't be disheartened if a particular
Council just won't affiliate. The point is to deny the Home Office access to
as much of Local Government as possible for as long as possible. Resistance
starts here!</p>

<p>For further advice and what assistance we are able to give, please phone the
office on 020 7340 6077 or send an e-mail to office&#x40;no2id.net.</p>



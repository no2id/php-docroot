<h1>Milton Keynes Council, First Motion</h1>
<h2>September, 2006</h2>

<p>This Council opposes the Labour Government's plans to introduce 
   National Identity Cards and a National Identity Database.</p>
   
<ol>
    <li>This Council is concerned at:</li>
    
    <li>
       <ol>
         <li>the amount of information that will be gathered in 
	     the National Identity Database,</li>
         <li>the ability for the Home Secretary to require more 
	     information on a whim,</li>
         <li>the lack of clarity of just who will have access 
	     to it and</li>
         <li> the purpose for which it may be used.</li>
       </ol>
    </li>
    
    <li>Labour's plans will fail to deal with many of the 
        reasons for which the Labour Government wishes to 
	introduce ID Cards, as they will have little or no 
	effect in fighting terrorism, crime and illegal 
	immigration.</li>

    <li>The introduction of the National Identity Card and 
        its related database will cost at least &#163;3 billion, 
	which could be better used elsewhere.</li>
</ol>

<p>This Council therefore urges the Labour Government to 
   abandon its ID Card plans and reassign the funds allocated 
   for their introduction to the front-line services that will 
   have a real impact on the safety and security of the residents 
   of Milton Keynes, and throughout our Country. These include 
   higher visibility community policing and greater crime 
   prevention. This council also requires the Chief Executive 
   to write to the MP's for each of the Milton Keynes 
   constituencies to request them to vigorously oppose the 
   introduction of National Identity Cards and a National 
   Identity Database.</p>
   

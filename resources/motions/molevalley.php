<h1>Mole Valley District Council</h1>
<h2>30th October 2008 Agenda Item 12 &#8211; Motion 7/2008 &#8211; ID Cards</h2>

<p>This Council notes that:</p>

<ol>
  <li>the present parliament has approved the Identity Card Act 2006.</li>
</ol>

<p>This Council believes that:</p>

<ol style="counter-reset: item 2;">
  <li>the centralized collection by the Government of personal data as
      part of ID cards process in addition to the continuing Government 
      Programme of centralized collection of personal data in other areas 
      infringes UK citizen's civil rights and adds to the risk of
      unauthorized and even criminal targeting of that personal data;</li>
  <li>the scheme will impose wholly disproportionate costs on this Council
      and other public bodies which will include both implementation and 
      operational costs;</li>
  <li>and that this will have a damaging effect on all residents of Mole
      Valley.</li>
</ol>

<p>This Council resolves, unless required to do so by law, to:</p>

<ol style="counter-reset: item 5;">
  <li>take no part in any pilot scheme or feasibility work in relation to
      the National Identity Card Scheme;</li>
  <li>establish as Council policy that National Identity Cards will not be
      required in order to obtain Council benefits or services;</li>
  <li>refuse to participate in the national database.</li>
</ol>


  <p><strong>The NO2ID Pledge</strong></p><br />
  
  <p>You might be prepared to go to gaol rather than have an ID card. But you can&#8217;t.</p>
  
  <p>David Blunkett has been smugly pronouncing that there will be no ID card martyrs because the 
            intent is to have a system of penalties &#8211; like monstrous parking 
            fines &#8211; hard to contest in court. So further punishments would 
            relate to failure to pay, not ID cards. That silly distinction is 
            currently irrelevant, since powers of direct compulsion have been 
            dropped, for now. It hasn&#8217;t stopped Mr Blunkett repeating it, 
            though.</p>
			
  <p>Subtler minds have been at work. The Home Office plans to make you to &#8220;volunteer&#8221;. 
            It hopes almost all the population will &#8220;volunteer&#8221;, before 
            most people have even noticed what is happening. Well before it rounds-up 
            and force-fingerprints a few pariahs. Official documents will one 
            by one be &#8220;designated&#8221;, so that you cannot get one without 
            at the same time asking to be placed &#8211; for life &#8211; on the 
            National Identity Register.</p>
			
  <p>The civil servant, Sylvanus Vivian who originated this idea in 1934 &#8211; yes, that&#8217;s 
            right, <em>nineteen thirty-four</em> &#8211; called it &#8220;parasitic 
            vitality&#8221;. In other words, the scheme is a vampire. It has no 
            life of its own, and thrives only if it feeds.</p>
			
  <p>There is its weakness. We, collectively, can choose to starve the 
            Identity and Passport Service. It only works smoothly if few are prepared 
            to face a little inconvenience to resist. It only works at all if 
            a large majority of the population can be hypnotised into thinking 
            that it is just routine, no big deal. If enough of us refuse to be 
            bled willingly, the beast will either starve or show its fangs.</p>

  <p>Already &#8220;e-Passports&#8221; have been used as a pretext to build 
            a chain of interrogation centres to service the ID scheme. But further 
            growth of the parasite will be harder to hide. Which is where you 
            come in.</p>
			
  <p>Making martyrdom hard, made resistance easy too. Actually breaking the law at this stage 
            is hard to do. There is scarcely any ID card law to break; it is designed 
            to be brought in silently by regulations, alongside administrative 
            changes.</p>
			
  <p>So that&#8217;s why NO2ID is suggesting a new form of non-violent direct action: pre-emptive 
            resistance. You can do something positive now. Something totally legal; 
            that has its own life, not determined by us, but by you. Anyone can 
            do it. Anyone can help others do it. The more who do, the easier it 
            is.</p>
			
  <p>You can resolve openly, and clearly, not to do those specific things that give the ID scheme 
            its &#8220;parasitic vitality&#8221;:</p>
			
  <p><em>&#8220;I solemnly and publicly promise that:</em></p>
  
  <p><em>&#8226; I shall not register for a national identity card<br />
         &#8226; I shall not supply personal details or fingerprints to a National 
            Identity Register<br />
         &#8226; I shall not apply for any document or service if joining the 
            National Identity Register is a condition of obtaining it<br />
         &#8226; I shall not co-operate with any Identity and Passport Service 
            interview concerning my identity</em></p>
			
   <p><em>I also promise by my example to encourage others to do the same.&#8221;</em></p>
   
   <p>In just one month of 2005, over 10,000 people pledged online not to register. Many more will 
            take this NO2ID Pledge, and pass it on to others. Maybe the Government 
            thinks it could force tens of thousands to submit by denying them 
            basic services. It would be a very brave Government that tried.</p>
			

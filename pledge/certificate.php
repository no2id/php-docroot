  <p><strong>Making The NO2ID Pledge</strong></p>
   
   <p>Simply take a printed copy of the pledge. Write your name clearly in the space provided on the 
            first line, and add the date where indicated at the end of the line. 
            (You might want to read it out loud.)</p>
			
   <p>Your witness &#8211; who could be anyone that you know, a friend, member of your family or 
            NO2ID supporter &#8211; should add his or her name in full on the 
            line below, and sign alongside it in the space marked (Signed).</p>
			
   
  <p>When you sign on the dotted line at the bottom of the page, you have made 
    The NO2ID Pledge. Make sure you let everyone know that you are standing up 
    for what you believe.</p>


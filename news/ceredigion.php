<h1>Ceredigion County Council joins growing list of local councils supporting
NO2ID </h1>
<p>From Norwich in the east to Wales in the west, local politicians and local
		people continue to join under the NO2ID banner to reject the government &ndash; and
any future government&rsquo;s &ndash; plans for a compulsory identity register.</p>
<p>Ceredigion County Council, on the west coast of Wales, yesterday voted decisively
		against ID cards, voting 27-5 in favour of a motion that described ID cards
		as &ldquo;an endemic loss of privacy and freedoms [that] will present dangers
		to marginalised, disenfranchised and disadvantaged people.&rdquo;</p>
<p>The motion&rsquo;s proposer, Liberal Democrat councillor Mark Cole, spoke
		at length of how the national database that underpins the identity card scheme
		would be excessively expensive, unwieldy and would facilitate criminal fraud
		and terrorism. Failures in the system &ndash; bound to happen on a project
		of such size and complexity, and especially with the government&rsquo;s appalling
		track record of large projects &ndash; would endanger whole sections of the
		economy and the continuity of citizen&rsquo;s lives, as travel, banking, local
		and central government and health services are brought to a halt.</p>
<p>Rural, sparsely populated areas such as Ceredigion, especially those areas
		with a high proportion of elderly residents are prime examples of how identity
		cards would become a heavy and unnecessary burden, with infirm or remote citizens
		forced to travel large distances to register on pain of imprisonment and fines.</p>
<p>Following Hazel Blear&rsquo;s statements recently, that &ldquo;UK Muslims
		should accept that people of Islamic appearance are more likely to be stopped
		and searched by police&rdquo;, it is also especially in the less cosmopolitan,
		rural areas that those who conform to someone&rsquo;s (police, concerned citizen)
		perception of &ldquo;a terrorist&rdquo; could be repeatedly stopped, searched
		and made to feel like a criminal for no other reason than they fitted a non-white,
		arbitrary stereotype.</p>
<p>Cllr Cole was quick to point out an important difference between any potential
		UK card and those in use on the continent: our lack of written constitution,
		denying us the safeguards against government intrusion on our lives that other
		countries enjoy.</p>
<p>&ldquo;Can we be so trusting of this and future governments without a written
		constitution to keep them in check?&rdquo; Cllr Cole said to the assembled
		council; &ldquo;Future governments could very easily use this apparatus against
		its citizens. To have such a system which gives such unprecedented power
		to the state is a step towards authoritarianism.&rdquo;</p>
<p>Ceredigion joins Watford last week and seven other local authorities to take
		a firm stance on the proposed ID scheme, Labour&rsquo;s &ldquo;tax on life&rdquo;.</p>
<p>NO2ID Aberystwyth spokesperson, Rachael Owen said:</p>
<blockquote>
		<p>&ldquo;We are delighted
				that Councillors almost unanimously recognised the social injustice that will
				be caused by government plans for compulsory Identity Cards.&nbsp; Using
				unproven technology the government will require everyone to surrender personal
				information to a database, accessible by many private and public services.&rdquo;</p>
</blockquote>

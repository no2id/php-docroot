<h1>Growing number of local authorities reject ID cards</h1>
<p>Another week, and another fresh round of local authorities wake up to the
		reality of what ID cards would mean to them and their electors, and vote to
		reject them.</p>
<p>Lancaster City Council passed motion against ID cards proposed by Liberal
		Democrat councillor Stuart Langhorn, which stated that ID cards and the register
		would be &quot;an affront to civil liberties&quot;, were &quot;likely to have
		a harmful effect on police and community relations&quot; and that &quot;there
		is little evidence to show that they will reduce benefit fraud, illegal immigration
		or crime.&quot;</p>
<p>A wrecking amendment suggested by Labour councillors that repeated the government's
		line on ID cards was voted down, and the motion was passed with support from
Liberal Democrats, Conservatives and Green party members.</p>
<p>Last week, Brighton and Hove City Council failed by a few votes to secure
		a similar motion against the ID scheme, and on Tuesday next week Watford Council
		will also vote on a measure condemning the controversial ID project. Lancaster
		City Council brings the number of local authorities publicly coming out on
		the side of civil liberties, privacy and sanity to seven, joining Norwich,
		Oxford, York, Cambridge, Liverpool and Sefton, Merseyside.</p>
<p>Phil Booth, national coordinator for NO2ID, said: </p>
<blockquote>
		<p>&quot;I hope councils continue
						to realise the immense impracticalities of the proposed ID card scheme,
				the rapidly rising costs for implementation that they will undoubtedly have
				to share, and the potential that a compulsory ID register and checking system
						has for causing deep divisions between their local authority and
				the community.&quot;</p>
</blockquote>

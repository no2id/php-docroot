<h1>The mother of all polls shows ID not wanted</h1>
<p>Less than a week after the Identity Cards Bill scraped through its second reading 
  by 31 votes under heavy assault from backbenchers, a <a href="https://www.no2id.net/news/newsblog/?p=263" target="_self">YouGov</a> 
  poll has shown support for the Bill heading south.</p>
<p>Two other independent polls since the election &#8211; from <a href="https://www.no2id.net/news/newsblog/?p=239" target="_self">ICM</a> 
  and the <a href="http://www.dailymail.co.uk/pages/live/articles/news/news.html?in_article_id=353541&in_page_id=1770" target="_blank">Daily 
  Mail</a> &#8211; have shown support for a national ID system collapsing as more 
  details about the scheme are revealed, but the questions in the YouGov poll 
  reveal how deep public rejection runs.</p>
<p>Only 45 per cent of respondents thought ID cards were a good idea, just 10 
  per cent thought that the government was capable of introducing them efficiently 
  (remembering well the (long line of government IT fiascos) whereas 84 per cent 
  felt that the system would cause a lot of disruption and inconvenience.</p>
<p>For the government to continue with this deeply flawed and unpopular Bill, 
  even in the face of sustained criticism from the technology industry (the Kablenet 
  report), learned academics (the LSE report), civil servants (the Information 
  Commissioner, Richard Thomas), MPs and the public, then it is clear that ID 
  cards have become a ministerial obsession.</p>
<p>The question must be asked of government: All your arguments in favour have 
  been seen, understood and dismembered &#8211; to press ahead under such conditions 
  only suggests ulterior motives. What are we not being told?</p>
<p>Perhaps the writing is on the wall: apparently Charles Clarke has the &#8220;<a href="http://www.politics.co.uk/domestic-policy/pm-has-full-confidence-in-clarke-$14004763.htm" target="_blank">full 
  confidence</a>&#8221; of the Prime Minister. I think we all know what that means.<br>
</p>

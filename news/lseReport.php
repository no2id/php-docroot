<h1>LSE report draws sane conclusions from insane government Bill</h1>
<p>A group of over a dozen senior academics from the London School of Economics
		have released <a href="http://www.lse.ac.uk/collections/pressAndInformationOffice/PDF/IDreport.pdf">a
		report that condemns the government&rsquo;s ID Cards Bill</a> (PDF),
		stating the legislation to be &ldquo;too complex, technically unsafe, overly
		prescriptive and lack a foundation of public trust and confidence&rdquo;, and
		that although &ldquo;the concept of a national identity scheme is supportable,
the current proposals are not feasible.&rdquo;</p>
<p>The report &ndash; prepared in consultation with representatives from business
		and industry, government suppliers, the DTI and Inland Revenue &ndash; is a
		detailed examination of the proposed government legislation, and the authors
come down hard on its numerous flaws. </p>
<p>All the stated aims of the Bill &ndash; protecting the citizen against terrorism,
		crime and identity theft, ensuring the legitimate use of health and benefit
		services, and clamping down on illegal immigration and working &ndash; are
		stated by the report to be better met by other means, such as increased spending
		on border officials, frontline policing and the security intelligence services.
		The projected cost of &pound;5.5bn are set to more than double &ndash; as the
		NHS IT system did &ndash; and these costs represent considerable investment
that could potentially be placed in the hands of police and immigration forces. </p>
<p>In some cases, terrorism and identity theft in particular, ID cards would
		increase existing problems; by centralising identity, providing a single document
		that would then be prone to error, corruption and forgery, identity theft is
made simpler. </p>
<p>NO2ID National Coordinator, Phil Booth, said:</p>
<blockquote>
		<p>&quot;The government are trying
						to nationalise our identities, opening us up to new dangers, and
				then getting us to pay for it. This report lays bare the technocratic delusions
				of the Home Office and flaws in a scheme that would have terrible consequences
				for the country. Let us hope that this unjust and unjustified Bill is now
				laid to rest, never to return.&quot; </p>
</blockquote>
<p>Biometrics, the report says, have &ldquo; never been used at such a scale&rdquo; and
		the system proposed would be &ldquo; technologically precarious and could itself
		become a target for attacks by terrorists&rdquo;. Other countries such as China
		and Korea that have previously implemented biometric technology are now reviewing
		its use due to the logistical problems involved. The airline Quantas used biometric
		checking for its staff, and found an error rate of between two and ten per
		cent &ndash; on the UK population of over 60 million at projected rates of
use, this would mean millions of errors a day. </p>
<p>The legitimacy of the proposals is also questioned. There is little accountability,
		the report says, with even the Commissioner for ID cards reporting to the Home
		Secretary and not Parliament. There exists incompatibility with a string of
		UK and EU laws, including the Data Protection Act, Race Relations Act, Disability
		Discrimination Act and European working and freedom to travel laws. And with
		so little time for Parliamentary scrutiny given to the Bill, whole sections
have gone unexamined. </p>
<p>The second reading of the Bill in the House of Lords on Monday saw their
		Lordships condemn the government for the way in which the Bill has been forced
		through Parliament. There is strong opposition to the Bill in the upper chamber,
		and Lords quoted from the report and from NO2ID literature. With potentially
		only a few weeks until the end of Parliament, the Bill is in for a turbulent
but brief ride.</p>

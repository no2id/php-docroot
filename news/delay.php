<h1><B>Government set to abandon ID Cards Bill until after election</B></h1>
<p>NO2ID looks to have achieved its first goal and has defeated the ID Cards
		Bill after months of public and behind the scenes lobbying.</p>
<p>Phil Booth, NO2ID National Coordinator, said:</p>
<blockquote>
		<p>"This is a great victory, although the fight goes on. We must continue to
				apply pressure until the Government abandons its plans altogether."</p>
</blockquote>
<p>According to a report in The Independent, an unnamed Government Minister has
		said that the ID Cards Bill will not be pushed through the House of Lords before
the end of Parliament and the General Election.</p>
<p> With only a handful of days, perhaps four or five, available for their Lordships
		to debate the bill, it was always going to be tight, following the Conservative
		Party's withdrawal of support for the bill.</p>
<p> The Government can now temporarily back off from a Bill that, if it is debated,
				looks sure to be torn to pieces by a hostile House of Lords, currently
		engaged in performing a very public hatchet-job on the Terrorism Bill. The
		government are not keen to receive two bloody noses in quick succession.</p>
<p> The timing of the election is a problem for the Government, as they risk
				losing two major pieces of controversial legislation at the eleventh
				hour, only to have to restart them after the election. But ID card legislation
				is not going away - having already poured millions of pounds into research
				and campaigning over several years, the Home Office will not be too concerned
				with having to wait another few months until after a new government is
		returned.</p>
<p> This government has made it clear that their agenda includes a succession
				of highly illiberal, controversial and divisive Bills, to be rammed through
				Parliament as quickly as possible, and this is not expected to change
		suddenly after May.</p>
<p>A Home Office statement release to NO2ID said:</p>
<blockquote>
		<p>&quot;The Government remains
						fully committed to the Identity Cards programme. We expect the 2nd
				Reading of the Identity Cards Bill to take place on 21st March.&quot;</p>
</blockquote>
<p> Although this is a victory in the short term if this ID Cards Bill falls,
				the pressure, support and perseverance of NO2ID, its members and supporters
				will be even more vital when faced with a new, empty Parliamentary year,
				ready to be filled with new ID card legislation. </p>

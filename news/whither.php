<h1>Wither ID cards?</h1>
<p>So the Prevention of Terrorism Act has been passed, after a day-and-night-and-day of bouncing the
		bill between the Houses of Parliament, scattering Lords and Commoners alike
		to snatch sleep on library tables and corridor benches. The Bill was passed
		largely as the government wanted, without a written expiration date and with
		only a promise to re-examine it. The deal had been done.</p>
<p>Will a similar fate befall the Identity Cards Bill? With broadsheet newspapers
		trumpeting the Bill&rsquo;s imminent demise, the truth is anything could happen
		in the &ldquo;wash-up&rdquo; period before Parliament is dissolved for a general
		election.</p>
<p>The Bill has been contested by Liberal Democrats, vociferous Labour and Conservative
		rebels, and NO2ID and its thousands of supporters. Business groups, academics,
		civil liberties activists and ethnic minority groups have queued up to declare
		ID cards to be a terrible, impractical and illiberal idea.</p>
<p>The Bill&rsquo;s passage through Parliament has not been a model of how things
		should be done:</p>
<p>Scrutiny Committee and Parliamentary debating time has been slashed, resulting
		in whole sections of the Bill going unexamined. The Home Affairs Select Committee
		and Joint Committee on Human Rights have highlighted problems with the Bill,
		and the withdrawal of Tory support leaves the only supporters of ID cards as
		the government front bench and their heavily disciplined Labour Legion of the
		Lost.</p>
<p>If rumours are true and the Bill is to be dropped, it will be because the
		government cannot afford another of its prize pieces of legislation to be savaged
		in the House of Lords, and would rather it slip through &ldquo;lack of time&rdquo; than
		to risk the Bill being publicly rubbished, and then vetoed.</p>
<p>The House of Lords, with its more independent, less party-centric approach
		has proved decisive in redrafting and amending defective legislation passed
		by the Commons, and NO2ID hopes that the Lords will continue to do so by voting
		against ID Cards, and not allowing last-minute deals that would guarantee an
unscrutinised bill becoming law.</p>
<p>The ID Cards Bill may be shortly consigned to the Hansard shredding room,
		but as one of Labour&rsquo;s Six Pledges should Labour win the election it
		will return again like the bad penny. NO2ID hopes that the MPs returned after
		the election are more in tune to their constituents&rsquo; opposition to the
		scheme, and more prepared to act on it.</p>

    <h2> <a name="whatNext"></a>What's Next?</h2>
    <p>The Parliamentary Select Committee on Home Affairs reports on Identity 
      Cards at the end of this month. Like all committees it has a government 
      majority, but we know that there are a significant number of sceptics of 
      all parties on the Committee. It should make interesting reading.</p>
    <p>The Queen's Speech is in November. The last one announced the Draft Bill, 
      and we expect an actual ID Bill announced in it this time round. However, 
      it is possible that it will be timetable for the autumn regardless, to give 
      it the maximum time to get through before a prospective general election 
      (which most commentators believe will be some time towards the end of the 
      first half of next year).</p>
    <p>We are reasonably confident that the Bill, when it comes, can be stopped 
      in the Lords--though that will take work. There was no mandate for it in 
      the government's last election manifesto, so the Lords has every reason 
      to exercise a restraining power on such a profound measure. After the General 
      Election, it is likely that the Labour government will be returned again, 
      in which case it seems very likely that the population database will be 
      back, perhaps under yet another new name.</p>
    <p>But if we can create delay we will have gained some time to bring the myriad 
      problems and oppressive details to public attention.</p>
    <h2><a name="stopPress"></a>Stop press</h2>
    <p> <a href="http://www.timesonline.co.uk/newspaper/0,,176-1190990,00.html">A 
      disturbing leak to the Sunday Times - a database of all children</a>.</p>
    <p> This fills a mysterious gap since the Blunkett Draft Bill doesn't appear 
      to cover children, AND it appears to be going to be introduced without warning 
      as an amendment on an existing Bill, AND it is proposed as a database not 
      just of children's names and addresses, but familial and other personal 
      details including prejudicial facts (possibly even rumours) about family 
      members. So if this is real and not just kite-flying, it will violate several 
      assurances we've been given about the National ID Register--before the Register 
      itself is even in existence.</p>

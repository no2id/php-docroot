    <h2><a name="whatHappened"></a>What just happened?</h2>
    <p>The Home Office's latest consultation exercise, that on its 
      <a href="http://www.homeoffice.gov.uk/inside/consults/current/index.html">Draft 
      Bill</a>, has just closed. It will take a while to &quot;consider&quot; 
      the result, but the conclusion is predictable: Responses will be said to 
      have been broadly in favour of the proposals. Some valuable technical points 
      will have been made that will be &quot;taken into account&quot; in legislative 
      proposals.</p>
    <p>We believe that the whole consultation process has been a 
      sham. So, apparently, does David Blunkett. The Scotsman newspaper reported--before 
      the period for submissions to the consultation closed:</p>
    <blockquote cite="http://news.scotsman.com/politics.cfm?id=826372004"> 
      <p>Mr Blunkett's officials also confirmed that, as Liberty 
        and other campaigners had warned, the planned national identity-card system 
        would form the foundation of a central data collecting and monitoring 
        system.</p>
      <p>Once the database for the biometric data stored on the cards 
        is established, it will be linked to the likes of the Police National 
        Computer, Interpol and the security services' &quot;warnings index&quot; 
        of those believed to be a threat to national security.</p>
    </blockquote>
    <p>As well as jumping the gun, this substantially extends the 
      scope of what has been proposed. It has never before been admitted that 
      the so-called id-database is to be linked to others. It is rather interesting 
      that access to the information on the register might be available to foreign 
      governments through Interpol, and that people may be flagged as &quot;believed 
      to be a threat to national security&quot; by the security services.</p>
    <p>Is the public process on one page, and the secret process 
      on another?</p>
    <p>Be that as it may, we believe it is important to show opposition 
      in detail as well as in principle. NO2ID presented a lengthy response to 
      the consultation, which will soon be available for download on <a href="http://www.no2id.net/">the 
      website</a>:</p>
    <p>On the plus side, NO2ID is formally established, has a bank 
      account, and the backing of a lot of committed people. Starting from scratch 
      only a few months ago, we are catching up the Home Office's 20-year start. 
      We are supported by respected organisations such as Liberty, Charter 88 
      and Privacy International, and several political parties (though the Conservatives, 
      as a party, are sitting on the fence)--<a href="/content/supporters.html">see 
      the website for details</a>.</p>
    <p>A public meeting &quot;Mistaken Identity&quot; was held at 
      the LSE on 19th May, organised by Privacy International and supported by 
      several other organisations. It was judged a great success by those who 
      attended. There was some positive press coverage. It certainly wasn't intended 
      as a rally, but any supporters of the database state failed to turn up. 
      The Home Office in particular refused all inducement to take part, and no 
      official spokesman for the Labour Party was available. (Though as individuals 
      Labour MPs and Peers made their presence felt--for the anti-government side.)</p>
    <p>David Davis, Shadow Home Secretary, in a thoughtful speech, said he was 
      undecided but would be in favour of ID-cards if they would stop terrorism. 
      (Since it can be shown they won't, it looks like Mr Davis may well be persuadable 
      to our part of the argument.) There were many impressive contributions among 
      them a deeply considered speech in opposition to the system from the Law 
      Society.</p>
    <p><a href="http://www.privacyinternational.org/conference/missingid/">Full 
      list of speakers, none of whom one could call a zealous advocate.</a></p>
    <p> Mr Blunkett should not be able to get away with characterising his opponents 
      as--in that revealing turn of phrase--&quot;the usual suspects&quot;. This 
      can be an establishment campaign. We are defending traditional rights and 
      freedoms.</p>
    <hr/>
    <p>You are probably aware of the <a href="http://www.privacyinternational.org/">poll 
      recently conducted by YouGov for Privacy International</a>. Government research 
      has confined itself to asking people vaguely if they think ID CARDS are 
      a Good Thing, allowing them to be triumphant about getting a majority 'yes'. 
      The PI Poll shows something much more interesting.</p>
    <p>The more people know about the project, and the National Identity Register, 
      the less they like it. And opposition is substantial. We are not alone.</p>
    <p>If you scale up the number of people who say they'd rather go to jail than 
      carry a compulsory ID card from the poll sample, then there are approximately 
      3 MILLION people for the Home Office to fine and jail. Maybe this is why 
      the plan is for the scheme to be introduced gradually at first--and by stealth.</p>

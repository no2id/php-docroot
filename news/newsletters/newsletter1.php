    <h2><a name="takeAction"></a>What you can do about it</h2>
    <p> Like any campaign we need funds and people. Any help or expertise of any 
      kind you can spare will be most welcome. Most important is to get other 
      people involved and to understand the issues. ID-CARDS sound innocent. When 
      people know that they (and their children) may be fingerprinted like criminals 
      and will be made to register every change of address with the secret police, 
      their attitude changes. So:-</p>
    <p>1. Tell your friends about the campaign and that you are supporting it. 
      Feel free to redistribute this newsletter to acquaintances. (But please 
      do introduce it personally and don't spam.) Your personal witness will help 
      others understand that this is an important real-life matter, and not just 
      the province of hyper-political nuts.</p>
    <p>2. Let us know if you have time or special expertise that you can give 
      to the organisation, even if only a couple of hours a week. We desperately 
      need an experienced Press Officer, secretarial staff, and policy researchers. 
      Professional creative and marketing expertise are badly wanted to lift public 
      awareness of the issues.</p>
    <p>3. Get together with others in your area to form a local group. A national 
      campaign needs a national network. <a href="mailto:guy&#x40;no2id.net">Guy Taylor</a> 
      can give you advice on how to do this. If you are in London come to the 
      open, pub-based, meetings that we hold every month on a Thursday somewhere 
      central. Details are posted on the website.</p>
    <p>4. Write to the local and national press, to express your concerns whenever 
      there is relevant news. Phone-in to appropriate radio and TV programs. There 
      is plenty of information on (and linked to) the website to help you do this.</p>
    <p>5. Write to your MP and MEP expressing your disquiet and ask for _their 
      personal opinion_. (Big-party MPs in particular sometimes avoid controversial 
      issues, or may pass on your letter to the Home Office rather than answering 
      for themselves.)</p>
    <p>6. Buy a T-shirt, and wear it. This publicises the message--people will 
      ask you what it means--and raises funds. <a href="/content/tshirt_order.html">Details 
      on the website</a>.</p>
    <p>7. Join. Membership costs &pound;15 a year and entitles you to a say in 
      the running of NO2ID. A membership form will appear on the website shortly.</p>

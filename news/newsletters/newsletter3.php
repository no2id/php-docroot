<h2><a name="commissioner"></a>The Information Commissioner Speaks Out</h2>
  <p>A real boost for objectors last week. In <a href="http://www.timesonline.co.uk/article/0,,2-1218615_2,00.html">an 
    interview with The Times</a>, Richard Thomas, the Information Commissioner, 
    who is in charge of overseeing the UK's data protection regime, attacked not 
    only the Home Office's National Identity Register but its shadow in the Office 
    of National Statistics and the newly-mooted Children's Act database. <q cite="http://www.timesonline.co.uk/article/0,,2-1218615_2,00.html">My 
    anxiety is that we don't sleepwalk into a surveillance society where much 
    more information is collected about people, accessible to far more people 
    shared across many more boundaries than British society would feel comfortable 
    with.</q> he said. He suggested comparisons with the former East German secret 
    police, the Stasi, were paranoiac at this stage... but invited them, nevertheless.</p>
  <p>The Commissioner's words were impressively strong ones for a British public 
    official and have generated a lot of interest. He has a little way to go however 
    before reaching the outspokenness of his equivalent in the Canadian province 
    of Alberta. According to the Calgary News: <q>[...]privacy commissioner, Frank 
    Work, says he would refuse to get one should they become mandatory. <q>I won't 
    carry one,</q> says Work. <q>They can come and get me.</q></q> </p>
  <h2><a name="CBI"></a>The CBI Mumbles</h2>
  <p>The <a href="http://www.cbi.org.uk/">Confederation of British Industry</a> 
    has published a <a href="http://www.cbi.org.uk/80256716004baae5/33a87f2eee41b54e80256803004f04e4/eba06badb02f983a80256ee600557828?OpenDocument">response 
    to the HO consultation</a>. Its conclusions are confusing. Big business likes 
    the idea of ID cards, but isn't really sure why. It is however uneasy about 
    the proposed system, not least because--like everyone else who has been paying 
    attention--the CBI has noticed that the Home Office won't say exactly what 
    it is trying to do, or why.</p>
  <p>We suggest that at least part of the confusion is because the Confederation 
    begins by accepting the need for, and value, of a centralised ID-system, and 
    feeding-back many of the Government's own assertions. It has taken on faith 
    that a single identity authentication system is a Good Thing and will work 
    as advertised--precisely what most critics have taken issue with. That, having 
    swallowed the Home Office's inconsistent premises and varied past pretexts, 
    it can't reconcile them with the obscure and far-reaching proposals in the 
    Draft Bill should not be a surprise. Neither can anyone else.</p>
  <p>There is one unequivocal point that stands out, however. And it is an important 
    one. Businesses should not be held liable if they use information from the 
    Identity Register that is wrong. We agree. Why should they? (Though we wonder 
    why they would expect to be using the
    information.)</p>
  <p>Will the Home Office admit that the Identity Register will go wrong, and 
    consider the consequences for individuals as well as businesses when it does?</p>
  <h2><a name="happened"></a>What Just Happened?</h2>
  <p>The Information Commissioner's comments were the occasion for Mark Littlewood 
    to appear on Five News for NO2ID, talking to Kirsty Young. A Government representative 
    did not join the studio debate but was separately interviewed by video-link. 
    As often on these occasions Five held a live phone-in poll: <q>Is Britain 
    in danger of becoming a surveillance state?</q> Yes, said 75%. While it is 
    certainly as unscientific as Martin Linton MP's own vox pop (<a href="/content/newsletters/newsletter2.htm#linton">Newsletter 
    No.2</a>) this at least shows public opinion isn't solidly behind the Government. 
    What people say depends on what the question is.</p>
  <hr>
  <p>Of interest in relation to the future uses of an Identity Register was a 
    <a href="http://www.lawreports.co.uk/civauge0.3.html">Court of Appeal Case 
    on 30th July</a>. It seems that under certain circumstances the police already 
    have wide discretion to disclose charges not resulting in a conviction.</p>
  <hr>
  <p>A little late we have picked up <a href="http://www.tfa.net/id001.pdf">The 
    Freedom Association's submission to the Home Office consultation</a>. It is 
    forcefully against in principle. Normally associated with the right of the 
    Conservative Party, TFA is a group that it is very hard for Mr Blunkett to 
    dismiss as airy-fairy and soft on crime.</p>
  <hr>
  <p>Otherwise August has been politically quiet, and we hope you've had a chance 
    to enjoy a break. Things may hot up as the weather cools down.</p>
  <hr>
  <p>We think Mr Blunkett's private life is his own business and have no interest 
    in it. We only wish his civil servants could be persuaded that the same is 
    true of the rest of us.</p>

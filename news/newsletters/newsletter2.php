      <h2><a name="happened"></a>What just happened?</h2>
      <p>NO2ID was well represented at Privacy International's Big Brother Awards 
        on 29th July at the LSE. Mark Thomas as host wore our colours for the 
        cameras, and a packed house bought our entire mobile stock of T-shirts. 
        <a href="http://www.theregister.co.uk/2004/07/29/big_brother_awards/">Results 
        are available on the Register.</a></p>
      <p>The House of Commons Select Committee on Home Affairs reported on the 
        current ID proposals as expected on 30th July. Its report (endorsed by 
        a majority of 5-2, with the two dissenting members producing a minority 
        report) seemed puzzling to many observers.</p>
      <p>The Register summed up neatly: &quot;<a href="http://www.theregister.co.uk/2004/07/30/id_card_report/">ID 
        cards: a bad idea, but we'll do it anyway</a>.&quot;</p>
      <p><a href="http://www.publications.parliament.uk/pa/cm/cmhaff.htm">The 
        official parliamentary site for the report itself</a>.</p>
      <p>Bob Russell and David Winnick's Minority Report appears as a <a href="http://www.publications.parliament.uk/pa/cm200304/cmselect/cmhaff/130/13012.htm">rejected 
        amendment</a> in the Formal Minutes at the end of the report.</p>
      <p>Most of the serious press seems to have got the &quot;bad idea&quot; 
        point, even if only on grounds of cost. Meanwhile, the Home Office merrily 
        got on with misrepresenting the cautious conclusions of the majority report 
        as thoroughgoing endorsement, and the views of critics as being favourable. 
        <a href="http://blunkettisanarse.blogspot.com/2004/07/blunketts-response.html">A 
        dissection of the technique in action</a>.</p>

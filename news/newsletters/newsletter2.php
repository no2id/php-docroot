      <h2><a name="linton"></a>Wierd and wonderful- Mr Linton's Independant Scheme</h2>
      <p>Next time someone tells you they are in favour of ID-cards, question 
        them closely. They may have something different in mind from the Home 
        Office. Mr Martin Linton (Lab), MP for Battersea is a fervent fan. <a href="http://www.wandsworthguardian.co.uk/news/localnews/display.var.515511.0.survey_boost_for_id_card_scheme.php">According 
        to the Wandsworth Guardian</a> he has been surveying local residents on 
        their attitudes to ID-cards by the highly scientific method of a self-designed 
        and self-administered questionnaire. The trouble is... that Mr Linton's 
        ideas of the functions of an ID-card are entirely unlike (and very, very, 
        much more ambitious than) those on offer from the Home Office. To his 
        credit David Blunkett is not planning to track your public library borrowings 
        in the near future.</p>

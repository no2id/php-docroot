<h2><a name="agm"></a>NO2ID Public Launch and Inaugral AGM</h2>
      <p>Though Mark Littlewood has been interviewed a number of times in his 
        role as National Coordinator, and we have had a presence at related events, 
        NO2ID has yet to be officially launched. We therefore open our public 
        campaign proper on <strong>Saturday 18th September</strong> with a rally 
        for supporters in central London and simultaneous smaller events elsewhere 
        in the country.</p>
      <p>You should shortly receive an invitation to the London rally, which will 
        consist of a meeting at a central location, followed by lunch and some 
        afternoon publicity activities. If you are elsewhere and would like to 
        attend, or to organise, something more local, please contact <a href="mailto:guy&#x40;no2id.net">Guy 
        Taylor</a> who can give you help and advice.</p>
      <p>NO2ID will be having its <strong>first Annual General Meeting on 9th 
        October</strong> in order formally to adopt its constitution and to elect 
        officers. As it is an unincorporated association, each paid-up member 
        of NO2ID will have a vote. Individual membership is open to anyone who 
        supports our aims and who pays an annual membership fee. Membership starts 
        at 15 GBP (with an 8 GBP concessionary rate). We have deliberately set 
        the rate low to make membership affordable to as many people as possible, 
        but encourage you to pay what you feel you can afford. <a href="/content/join.html">Membership 
        forms are available on the NO2ID website</a>, or you can write in with 
        your name and address, enclosing a cheque for your membership fee to NO2ID. 
        It is our policy not to pass on the details of members and supporters 
        to any outside organisation.</p>

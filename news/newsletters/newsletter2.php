      <h2><a name="next"></a>What's Next?</h2>
      <p>We will shortly be hitting the political conference season. As well as 
        our own events, there are already plans in train for us to hold a workshop 
        and have a stall at the European Social Forum and to stage a fringe meeting 
        at the Labour Party conference in Brighton. Anyone who'd like to help--particularly 
        Labour Party members who may be attending the Conference--please get in 
        touch.</p>
      <hr/>
      <p>The Conservatives are in Blackpool this year from 4th- 7th October. On 
        ID-cards of late they have sounded more and more sceptical of Mr Blunkett's 
        vague assertions, despite apparently trying to keep up with the Government's 
        fierce &quot;law-and-order&quot; drive. We are actively working to encourage 
        and coordinate opposition from across the political spectrum, so watch 
        out for developments here. Polls do seem to show that Tory supporters 
        are more likely than average to be suspicious of the ID-system. Again 
        it would be good to hear from attendees.</p>

<h1><a name="reject"></a>Scottish Parliament rejects ID cards</h1>
<p>Yesterday the Scottish Parliament voted 52 - 47 to reject the Government's
		ID card proposals in a motion proposed by the Scottish Greens and supported
		by every single party, with even one Labour rebel voting against the party
line.</p>
<p>Although the Scottish Liberal Democrats officially abstained, stating that
		the Green motion <em>did not got far enough</em>, such strong cross-party support shows
		a clear strength of feeling against ID cards in Scotland. It raises the spectre
		of the Poll Tax, that great failure of an idea that brought down Margaret Thatcher's
		Conservative government after widespread dissent spread south from Scotland
		where the tax had been trialed.</p>
<p>Patrick Harvie, Scottish Green MSP and proposer of the motion spoke about
		the serious concerns the Bill raised over privacy and civil rights. He dismissed
		claims that law-abiding citizens had nothing to worry about, asking:</p>
<blockquote>
		<p>&quot;Would you feel the same if the state wanted to read your mail? After
						all if you've got nothing to hide, you've got nothing to fear. Would
				you feel the same if the state wanted to search your home? After all if you've
				got nothing to hide, you've got nothing to fear.&quot;</p>
</blockquote>
<p>Members of <a href="http://www.no2id-scotland.net/">NO2ID Scotland</a> were at the Scottish Parliament before the vote,
		raising awareness of the dangers of ID cards among MSPs and the public.</p>
<p>The passing of the motion demonstrates the serious concerns that MSPs hold
		over the lack of Parliamentary scrutiny given to the ID Cards Bill and the
		speed at which the legislation has been rammed through Parliament before a
		General Election, in addition to the spiralling costs and doubts over ID cards'
		suitability for any of the purposes for which they have been suggested.</p>
<p>Phil Booth, national coordinator of NO2ID said:</p>
<blockquote>
		<p>&quot;Scotland's devolved
						powers have already been used to ensure that ID cards will not be
				required for access to Scottish services, and English local authorities are
				following suit.</p>
		<p>&quot;The decision of the Scottish Parliament should serve as a stark warning
						to UK politicians as to the very real opposition to ID cards and
				the identity register that they will face in this or any future Parliamentary
				session.&quot;</p>
</blockquote>

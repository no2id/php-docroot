  <h2> <a name="8"></a>Will an identity card help eliminate benefit fraud?</h2>
  <p>The government has not provided evidence to support such a claim. David Blunkett 
	went so far as to dismiss the significance of identity fraud in welfare, <a href="http://www.publications.parliament.uk/cgi-bin/ukparl_hl?DB=ukparl&URL=/pa/cm200102/cmhansrd/vo020703/debtext/20703-05.htm">advising 
	Parliament</a> &#8220;benefit fraud is only a tiny part of the problem in 
	the benefit system&#8221;. The majority of fraud on the benefits system is 
	through under-reporting of income, or non-reporting of financial and family 
	circumstances. Benefits agencies worldwide agree that false identity is not 
	a key issue.<a href="http://www.privacy.org/pi/activities/idcard/idcard_faq.html"> 
	The Australian Department for Social Security estimated</a> that benefit overpayment 
	by way of false identity accounts for only 0.6 per cent of overpayments, whereas 
	non-reporting of income variation accounts for 61 per cent.</p>
  <p><a href="http://www.parliament.the-stationery-office.co.uk/pa/cm200304/cmselect/cmhaff/uc130-vi/uc13002.htm">In 
	evidence to the Home Affairs Committee</a>, the Parliamentary Under-Secretary 
	at the Department of Work and Pensions, Chris Pond MP, confirmed that false 
	identity represented a tiny fraction of the benefit fraud problem. He said 
	his Department advised that of the estimated £2 billion total annual benefit 
	fraud, £50 million came from people not being who they said they were when 
	making a claim.</p>
  <p>It is likely that the cost to government of establishing a new ID infrastructure 
	for benefits would amount to many times the annual loss through false identity.</p>
  <h2><a name="9"></a>Will an identity card help prevent terrorism?</h2>
  <p>The government appears to have no idea. On July 3rd 2002, <a href="http://www.publications.parliament.uk/cgi-bin/ukparl_hl?DB=ukparl&URL=/pa/cm200102/cmhansrd/vo020703/debtext/20703-05.htm">in 
	response to a question by Chris Mullin MP, David Blunkett said</a> &#8220;I 
	accept that it is important that we do not pretend that an entitlement card 
	would be an overwhelming factor in combating international terrorism&#8221;. 
	Six minutes later, in answer to a question from Sir Teddy Taylor MP, he said 
	he would not rule out the possibility of &#8220;their substantial contribution 
	to countering terrorism&#8221;.</p>
  <p>The Government’s current line is that an ID card will help in the fight against 
	terrorism. However the essential facts are disputed. David Blunkett has told 
	parliament that the security services have advised him that 35 per cent of 
	terrorists use false identification, However <a href="http://www.theregister.co.uk/2004/12/02/noble_wows_lords/">Interpol 
	general secretary Ron Noble told the House of Lords Home Affairs Committee</a> 
	that all terrorist incidents involve a false passport. He was unable to present 
	evidence to support this claim. The published evidence refutes these claims. 
	In 2004 <a href="http://www.privacyinternational.org/issues/idcard/uk/id-terrorism.pdf%20">Privacy 
	International published the findings</a> of the only research ever conducted 
	on the relationship between identity cards and terrorism. It found that there 
	was no evidence to support the claim that identity cards can combat terrorist 
	threats.</p>
  <p>The report stated: &#8220;The presence of an identity card is not recognised 
	by analysts as a meaningful or significant component in anti-terrorism strategies.&#8221;</p>
  <p>&#8220;The detailed analysis of information in the public domain in this 
	study has produced no evidence to establish a connection between identity 
	cards and successful anti-terrorism measures. Terrorists have traditionally 
	moved across borders using tourist visas (such as those who were involved 
	in the US terrorist attacks), or they are domicile and are equipped with legitimate 
	identification cards (such as those who carried out the Madrid bombings).&#8221;</p>
  <p>&#8220;Of the 25 countries that have been most adversely affected by terrorism 
	since 1986, eighty per cent have national identity cards, one third of which 
	incorporate biometrics. This research was unable to uncover any instance where 
	the presence of an identity card system in those countries was seen as a significant 
	deterrent to terrorist activity.&#8221;</p>
  <p>&#8220;Almost two thirds of known terrorists operate under their true identity. 
	The remainder use a variety of techniques to forge or impersonate identities. 
	It is possible that the existence of a high integrity identity card would 
	provide a measure of improved legitimacy for these people.&#8221;</p>
  <p>&#8220;At a theoretical level, a national identity card as outlined by the 
	UK government could only assist anti-terrorism efforts if it was used by a 
	terrorist who was eligible and willing to register for one, if the person 
	was using their true identity, and if intelligence data could be connected 
	to that identity. Only a small fraction of the ninety million crossings into 
	the UK each year are supported by comprehensive security and identity checks.&#8221;</p>
  <p>The Bill also contains a fundamental condition that nullifies most of its 
	claims to support counter-terrorism. <a href="http://www.publications.parliament.uk/pa/cm200304/cmselect/cmhaff/130/4050405.htm">David 
	Blunkett has told the Home Affairs Committee</a> that in order to prevent 
	the creation of &#8220;ID card martyrs&#8221; the government would not make 
	it a criminal offence to refuse to be registered for a card. Instead, refuseniks 
	would be liable for a civil penalty. In view of the widespread and entrenched 
	hostility to the scheme, this approach makes tactical – and politically essential 
	- common sense. However, <a href="http://www.telegraph.co.uk/opinion/main.jhtml?xml=/opinion/2004/12/03/do0303.xml">astute 
	critics have pointed out that </a>wealthy people or those backed by criminal 
	organisations can avoid an ID card or registration simply by paying the recurring 
	£2,500 fine. This fine would effectively become a tax on criminals and terrorists 
	operating in the UK.</p> </p>
  
<p>The government appears to be incrementally backing away from its original assertion 
  that the card system would be a tool to prevent terrorism. <a href="http://management.silicon.com/government/0,39024677,39126226,00.htm">In 
  a recent press briefing, Home Office minister Des Browne said</a> "It (the ID 
  system) does not stop it but it helps you police it and interdict it".</p>
  <h2><a name="10"></a>Will the ID system reduce the problem of identity theft?</h2>
  <p>The government has heavily promoted the need to combat the problem of identity 
	theft, consistently <a href="http://www.homeoffice.gov.uk/docs/id_fraud-report.pdf">citing 
	its estimate</a> of £1.3 billion per year lost because of the activity of 
	identity fraudsters.</p>
  <p>While identity theft is indeed a crime that can have a devastating impact 
	on the victim, this estimate has been called into question. A <a href="http://www.lawsociety.org.uk:80/influencinglaw/policyinresponse/view=article.law?DOCUM%20ENTID=166478">2004 
	conference organised by the Law Society</a> heard that the figure was derived 
	from a &#8220;best guess&#8221;. When the data is more closely analysed, it 
	dissolves. Credit card fraud is frequently confused with identity theft, and 
	estimates of the two activities frequently intersect.</p>
  <p>At first sight it appears logical to argue that a national identity system 
	will help combat identity theft. There is, however, a substantial body of 
	evidence to show that the establishment of centralised identity can increase 
	the incidence of identity theft.</p>
  <p>The clearest example of this relationship exists in the United States, where 
	<a href="http://informationweek.com/story/showArticle.jhtml?articleID=54800697">the 
	Social Security Number has become an identity hub</a> and a central reference 
	point to index and link identity. Obtaining a person’s SSN provides a single 
	interface with that person’s dealings with a vast number of private and public 
	bodies. Hence the level of identity theft in the US is extremely high.</p>
  <p><a href="http://www.privacy.gov.au/news/speeches/sp5_04p.html">This situation 
	applies equally in Australia</a>, where the introduction of a Tax File Number 
	has also increased the incidence of identity theft beyond the levels experienced 
	in the UK.</p>
  <p>The key factor behind identity theft is the widespread availability of a 
	central number, linked to a range of personal information. Consumer groups 
	in the US have recently criticised the Senate Banking Committee for failing 
	to take action to reverse this trend. <a href="http://www.consumersunion.org/pub/core_financial_services/000407.html">The 
	Consumers Union argues</a> that identity theft will continue to rise until 
	the relationship between the SSN and the publication of personal details in 
	the finance sector can be reduced.</p>
  <h2><a name="11"></a>How much will the scheme cost the taxpayer?</h2>
  <p>The government estimated in 2002 that the scheme would cost somewhere in 
	the order of £3.1 billion. When in 2004 the Home Affairs Committee asked the 
	Home Secretary to clarify the exact amount he refused, citing commercial secrecy. 
	By the time the final Bill was published in November 2004 <a href="http://news.ft.com/cms/s/fbc6527a-4276-11d9-8e3c-00000e2511c8.html">the 
	government acknowledged</a> that the cost of the scheme over ten years would 
	be £5.5 billion. <a href="http://www.computerweekly.com/Article134763.htm">Industry 
	specialists have warned</a> that the complexity and uncertainty of the scheme&#8217;s 
	architecture and technology could drive this cost even higher. 
  <h2><a name="12"></a>Who will pay for this scheme?</h2>
  <p>We will pay for it largely out of our own pockets. An &#8220;enhanced&#8221; 
	biometric passport, which includes entry on the national register, will cost 
	around £85. An ID card without a passport will <a href="http://news.bbc.co.uk/1/hi/uk_politics/4034699.stm">on 
	current estimates</a> cost between £35 and £40. There will be a charge for 
	the renewal or replacement of cards.</p>
  <p>Clause 37 also allows the Secretary of State (with the permission of the 
	Treasury) to pass regulations to apply additional charges for a range of circumstances 
	such as disclosure of information and modification of information on the register.</p>
  <h2><a name="13" id="13"></a>Is the card compulsory or voluntary?</h2>
  <p>The Home Office has been clear that the intention has always been to create 
	a compulsory regime, but until recently the government has convoluted this 
	crucial point. Government ministers have almost unanimously ruled out the 
	option for legal compulsion to carry a card, and indeed clause 15 (3) of the 
	Bill specifically rules out any provision (within the ID cards Bill) for requiring 
	people to carry the card at all times. This clause also rules out compulsion 
	to submit a card to receive a benefit or any public service. However, this 
	clause does not provide protection to anyone who has been ordered to register 
	for a card under the &#8220;compulsion&#8221; clause of the Bill. Following 
	approval of an order, 6 (1) empowers the Secretary of State to order anybody 
	or everybody to register for a card. This might include benefits recipients, 
	new employees, people wanting to open a bank account, people of a particular 
	ethnicity, people who have been in contact with law enforcement or, indeed, 
	the entire population. Although the government has speculated that this clause 
	may not be brought into force for some years, there is no time period established 
	in the Bill. Parliament could approve the order to do so at any time it wishes.</p>
  <p>The government's stated definition of &#8220;compulsory&#8221; used to be: 
	&#8220;not required to be carried by each individual at all times&#8221;. 
	Now the official line is that the card will eventually become universal and 
	compulsory. That is, it will become compulsory to be entered onto the National 
	Identification Register. Clause 2 (4) of the Bill allows the Secretary of 
	State to enter a person onto the National Identity Register without that person’s 
	consent. Clause 5 allows the Secretary of State to propose &#8220;designated 
	documents&#8221; that will require entry onto the Register. This power will 
	apply, for example, when a person applies for or renews a passport or when 
	a foreign national seeks a residence permit. Passport holders will automatically 
	be entered onto the identification register. For those people who do not have 
	a passport 6 (1) will allow the government to require you to be registered.</p>
  <p>In <a href="http://www.publications.parliament.uk/pa/cm200304/cmselect/cmhaff/130/13002.htm">its 
	final report on the Draft Identity Cards Bill</a> the Home Affairs Committee 
	warned: &#8220;The move to compulsion is a step of such importance that it 
	should only be taken after the scrutiny afforded by primary legislation: the 
	proposed &#8220;super-affirmative procedure&#8221; is not adequate.&#8221; 
	The Committee urged the government to consider compulsion only through the 
	introduction of fresh legislation. This recommendation was entirely rejected 
	by the government. In fact, the Home Secretary pre-empted even the limited 
	mandate of Parliament by <a href="http://www.homeoffice.gov.uk/n_story.asp?item_id=1124">issuing 
	a statement</a> in which he said: &#8220;I will now bring forward legislation 
	to bring in a compulsory, national ID card scheme.&#8221;</p>
  <h2><a name="14" id="14"></a>Would children be required to have a card?</h2>
  <p>The government has addressed the matter of issue of cards for children from 
	the age of 5. In <a href="http://www.homeoffice.gov.uk/comrace/identitycards/publications.html">its 
	consultation paper</a> it identified 36 possible uses of cards in such circumstances 
	as entry to &#8220;12 Certificate&#8221; films and ownership of a pet.</p>
  <p>The Bill establishes the minimum age for card registration at 16 years and 
	three months. However, 2 (7) of the Bill permits the Secretary of State by 
	order to lower the minimum age. This option may be pursued. The government’s 
	consultation paper states: &#8220;For an entitlement card scheme to be an 
	effective proof of age card, it would need to be available to young people 
	over the full range of age restrictions that apply to various goods and services&#8221;. 
  
<p>Children&#8217;s rights groups have <a href="http://www.arch-ed.org/chldrnbill.htm">expressed 
  concern</a> that provisions in the Identity Cards Bill may allow a link with 
  data held in the forthcoming national children&#8217;s database permitted by 
  the Children&#8217;s Act. <a href="http://www.out-law.com/php/page.php?page_id=childrenbillrepeat1096381311&area=news">The 
  Children&#8217;s Act has been criticised</a> by <a href="http://www.publications.parliament.uk/pa/jt200304/jtselect/jtrights/161/16102.htm">the 
  Parliament&#8217;s Joint Committee on Human Rights</a> over its potential breach 
  of the right to privacy.</p>
  <h2><a name="15" id="15"></a>What if I simply refused to use the card?</h2>
  
<p>You will not be required to use a card unless you wish to work, use the banking 
  or health system, travel or receive benefits. <a href="http://www.publications.parliament.uk/pa/cm200102/cmhansrd/vo020703/debtext/20703-07.htm">As 
  Mr Blunkett advised Parliament</a>: &#8220;The issuing of a card does not force 
  anyone to use it, although in terms of drivers or passport users, or if services 
  - whether public or private - required some proof of identity before expenditure 
  was laid out, without proof of identity and therefore entitlement to do it I 
  doubt whether non-use of it would last very long.&#8221;</p>
  <p>It is important to keep in mind that the card will be buttressed by a vast 
	array of new state powers and criminal penalties. The Bill creates a score 
	of new offences including refusal to obey an order from the Secretary of State 
	(6(4)), failure to notify authorities about a lost, stolen, damaged or defective 
	card (13(1)), failure to renew a card (9(2)), failure to submit to fingerprinting 
	(9(4)(b)), failure to provide information demanded by the government (9(4)(d)), 
	failure to attend an interview at a specified place and time (9(4)(a)) and 
	failure to notify the Secretary of State of any change in personal circumstances 
	(including change of address) (12(1)). Failure to obey an order to register 
	or providing false information will also constitute an offence. Penalties 
	range from £1,000 fine to two years imprisonment. A penalty of up to £2,500 
	can be levied for failure to attend an appointment for a scan of your fingerprints 
	and iris. This fine can be repeated for every subsequent failure to attend.</p>
  <h2><a name="16" id="16"></a>How will the penalties be applied?</h2>
  <p>Many of the offences set out in the Bill are civil penalties. That means 
	it is unlikely you will secure legal aid to help your defence. You can object 
	to the penalty by writing to the Home Office, but the Secretary of State has 
	the right to increase the penalty if you choose to do so (34(3)). If you have 
	the financial means to bring a case, you can appeal to the courts (35(1)).</p>
  <h2><a name="17" id="17"></a>Can an ID card be forged?</h2>
  <p>Invariably, yes. The technology gap between governments and organised crime 
	has now narrowed to such an extent that even the most highly secure cards 
	are available as blanks weeks after their introduction. Criminals and terrorists 
	can in reality move more freely and more safely with several fake "official" 
	identities than they ever could in a country using multiple forms of "low-value" 
	ID such as a birth certificate. Forgery of ID cards has been detected and 
	prosecuted even where terms of imprisonment of ten years are threatened.</p>
  
<p>Prosecutions for dealing with or creating false ID cards and high-level identity 
  documents have been pursued <a href="http://news.bbc.co.uk/1/hi/uk/548559.stm">in 
  Britain</a>, <a href="http://www.thestandard.com.hk/news_detail_frame.cfm?articleid=52102%2B%22catid=42">Hong 
  Kong</a>, <a href="http://www.jang.com.pk/thenews/oct2003-daily/29-10-2003/main/main13.htm">Pakistan</a>, 
  <a href="http://www.irlgov.ie/debates-03/1Apr/Sect10.htm">Ireland</a>, <a href="http://www.mmail.com.my/Current_News/MM/Friday/National/20041210100244/Article/index_html">Malaysia</a>, 
  <a href="http://news.bbc.co.uk/2/hi/middle_east/1058085.stm">Yemen</a>, <a href="http://www.mvcr.cz/dokumenty/bezp_si00/angl/crime2.html">Czech 
  Republic</a>, <a href="http://www.analitica.com/archivo/vam1996.06/asleg1us.htm">Venezuela</a>, 
  <a href="http://www.kashmirsentinel.com/june2003/18.html">India</a>, <a href="http://www.chinadaily.com.cn/en/doc/2003-12/21/content_292183.htm">Italy</a>, 
  and <a href="http://www.dailynews.lk/2004/08/10/new24.html">Sri Lanka</a> where 
  the forgeries were supplied to suicide bombers. This year <a href="http://www.globes.co.il/DocsEn/did=834696.htm">the 
  Israeli government estimated</a> that &#8220;hundreds of thousands&#8221; of 
  fake ID cards are in the hands of its population.</p>
  <p>In many cases the false identity was secured merely by bribing an official 
	or by providing counterfeit documentation at the point of registration.</p>
  <h2><a name="18" id="18"></a>What is a &#8220;biometric&#8221; system and how 
	could it prevent forgeries?</h2>
  <p>A biometric is a measure of identity based on a body part or a behaviour 
	of an individual. The most well known biometrics are fingerprints, iris scans 
	and signatures. The government says some biometrics are extremely secure and 
	reliable forms of ID, and it appears very keen on the idea of fingerprints 
	and iris scans to establish one&#8217;s identity or, at least, one&#8217;s 
	uniqueness.</p>
  <h2><a name="19" id="19"></a>What would the biometric mean in practice?</h2>
  <p>Your biometrics would be taken upon application for a card and for entry 
	on the National Identification Register, and would be used thereafter for 
	major &#8220;events&#8221; such as obtaining a driving license, passport, 
	bank account, benefits or employment. Your eye would be scanned, and matched 
	both against the biometric on your card, and against a national database.</p>
  <h2><a name="20" id="20"></a>Have similar schemes been proposed or adopted in 
	other countries?</h2>
  
<p>Yes. <a href="http://www.vericardsys.com/MyKad.htm">Malaysia</a>, Singapore 
  and Thailand have very similar card systems. <a href="http://english.peopledaily.com.cn/200403/30/eng20040330_138863.shtml">China</a> 
  is moving rapidly in this direction with the development of a compulsory ID 
  database and card system but <a href="http://www.cardtechnology.com/cgi-bin/readstory.pl?story=20030911CTDN261.xml">abandoned 
  the biometric element</a> after it concluded that the technology was unworkable 
  with large populations. The US military in Iraq is developing <a href="http://www.theregister.co.uk/2004/12/09/fallujah_biometric_id/">a 
  similar card and biometric system to control access to Fallujah</a>, while the 
  <a href="http://www.un.org.pk/unhcr/press/Oct_10_03.htm">UNHCR</a> has deployed 
  an iris biometric system to control refugee traffic across the Pakistan-Afghan 
  border. The <a href="http://www.biometritech.com/enews/031903d.htm">UAE</a> 
  also uses an iris system for border control.</p>
  <p>However <a href="http://www.publications.parliament.uk/pa/cm200304/cmselect/cmhaff/130/130we37.htm">no 
	Common law country in the world</a> has ever accepted the idea of a peacetime 
	ID card. The <a href="http://www.anu.edu.au/people/Roger.Clarke/DV/OzCard.html">Australian</a> 
	and <a href="http://www.scit.wlv.ac.uk/%7Ec9479633/cp3349/smrtid.html">New 
	Zealand </a>public have rejected similar proposals outright. Following <a href="http://www.canoe.ca/CNEWS/Canada/2003/10/06/218966-cp.html">widespread 
	criticism</a>, Canada abandoned its proposed biometric ID card system in early 
	2004. ID card proposals have always been rejected by the United States Congress. 
	No European country has such a comprehensive or invasive card system.</p>
  <h2><a name="21" id="21"></a>Are there any legal protections to limit the scope 
	of an ID card?</h2>
  <p>Not really. The Data Protection Act provides some limited protections for 
	the individual, but will not prevent the government from establishing an ID 
	system. The first principle of the Act allows any such scheme provided legislation 
	has been passed to regulate the card and its uses. The others merely set out 
	<a href="http://www.hmso.gov.uk/acts/acts1998/80029--l.htm#sch1ptI">fringe 
	limitations</a> on how data can be used, rather than whether data can be collected 
	or matched.</p>
  <p>There would, for example, be no fatal legal barrier - including the provisions 
	of the Human Rights Act - to the government using a card and numbering system 
	to create a &#8220;real time&#8221; link between employment, bank account 
	data, the Home Office and Inland Revenue. The government <a href="http://www.dca.gov.uk/majrep/datasharing/update.htm">has 
	already signalled such intentions </a>through the recent Cabinet Office report 
	&#8220;Privacy & Data Sharing&#8221;. The consultation makes clear that the 
	government believes the ID card plan complies &#8220;with its obligations 
	under the Human Rights Act.&#8221;</p>
  <h2><a name="22" id="22"></a>This scheme will entail an unprecedented use of 
	complex technology. Is the government capable of achieving this successfully?</h2>
  
<p>The government has failed to successfully implement almost every large IT system 
  it has ever undertaken, resulting in <a href="http://www.guardian.co.uk/uk_news/story/0,3604,517008,00.html">the 
  loss to the public</a> of more than a billion pounds between 1997 and 2000. 
  <a href="http://www.publications.parliament.uk/cgi-bin/ukparl_hl?DB=ukparl&URL=/pa/cm200102/cmhansrd/vo020703/debtext/20703-05.htm">David 
  Blunkett told Parliament</a>: &#8220;I agree that it is important to recognise 
  the past failures of Government technology systems&#8221;.</p>
  <p>Billions of pounds of public money are wasted each year on such failures. 
	<a href="http://www.publictechnology.net/modules.php?op=modload&name=News&file=article&sid=2087">The 
	Child Support Agency</a>, Passport Agency, <a href="http://news.bbc.co.uk/1/hi/uk_politics/661987.stm">Inland 
	Revenue</a>, the <a href="http://www.computerweekly.com/articles/article.asp?liArticleID=133395&liArticleTypeID=1&liCategoryID=2&liChannelID=28&liFlavourID=1&sSearch=&nPage=1">Court 
	Service</a>, <a href="http://www.pcw.co.uk/news/1155581">National Air Traffic 
	Services</a>, the <a href="http://www.findarticles.com/p/articles/mi_m0COW/is_2004_Feb_17/ai_n6027823">Criminal 
	Records Bureau</a>, local authorities, the Ministry of Defence, the <a href="http://www.theregister.co.uk/2001/02/15/govt_scraps_163_80m_computer/">Home 
	Office</a>, the Prison Service. The list goes on and on. Each expensive project 
	promises to deliver better and cheaper services to the public. Each inevitably 
	fails to do so.</p>

  <h2><a name="1"></a>What stage has been reached with the ID card proposal?</h2>
  <p><a href="http://www.publications.parliament.uk/pa/cm200506/cmbills/009/2006009.htm">The Identity Cards Bill</a> has now passed its Committee Stage, with the Government having blocked every significant amendment.</p>
  <p>It will be presented to the House of Commons for its last stages there in the autumn. If it passes it will then go to the Lords. The earliest it could possbly pass into law now is the end of 2005.</p>
  <p> The Bill is similar in many respects to the <a href="http://www.homeoffice.gov.uk/comrace/identitycards/publications.html">Draft 
			Identity Cards Bill</a> that the Government published in April 2004 following <a href="http://www.homeoffice.gov.uk/comrace/identitycards/publications.html">the 
			public consultation</a> and <a href="http://www.publications.parliament.uk/pa/cm200304/cmselect/cmhaff/130/13002.htm">the 
			Home Affairs Committee hearings</a>. </p>
  <h2><a name="2"></a>Exactly what is the Government proposing to do?</h2>
  <p>Many people believe that the government&#8217;s ID scheme is just a &#8220;simple 
	piece of plastic&#8221;. The Identity Cards Bill shows clearly that the plastic 
	card is only one small part of a much larger scheme. The government is planning 
	to establish a vast, complex and far-reaching system that will involve an 
	unprecedented use of personal information.</p>
  <p>The Bill outlines an identity system that has eight components.</p>
  <ol>
	<li> <b>The National Identification Register.</b> The heart of the system. 
	  Clause 1 of the Bill imposes an obligation on the Secretary of State to 
	  establish a central population register containing a wide range of details 
	  of every UK citizen and resident aged from 16 years and 3 months.</li>
	<li> <b>The code.</b> Clause 2 (6) requires that every individual must be 
	  given a unique number, to be known as the National Identity Registration 
	  Number (NIRN). This number will become the &#8220;key&#8221; for government 
	  and private sector organisations to access information on the register and 
	  to share that information.</li>
	<li> <b>Biometrics.</b> Clause 5 (5) requires individuals to submit to fingerprinting 
	  and &#8220;other&#8221; means of physical identification. This is likely 
	  to include electronic face scanning and iris recognition.</li>
	<li> <b>The card.</b> Clause 8 establishes the actual identity card, generated 
	  from and containing the information in the Register.</li>
	<li> <b>Legal obligations.</b> Clause 15 establishes a requirement to produce 
	  the card in order to obtain public services.</li>
	<li> <b>Administrative convergence.</b> The number and the card register are 
	  used by a variety of agencies and organisations as their administrative 
	  basis. 1 (5) permits the bringing together of all registration numbers (National 
	  Insurance, NHS, etc) used by a person.</li>
	<li> <b>Cross notification.</b> Agencies will be required to notify each other 
	  of changes to a person's details. Clause 19 authorises the Secretary of 
	  State to disclose details from the register to other agencies without the 
	  consent of the individual.</li>
	<li> <b>New crimes and penalties.</b> The Bill establishes a large number 
	  of new crimes and offences to ensure that people comply with the ID requirements.</li>
  </ol>
  <p>These elements are set out clearly in clause 5 of <a href="http://www.homeoffice.gov.uk/docs3/ria_251104.pdf">the 
	Regulatory Impact Assessment for the Bill</a>.</p>
  <h2><a name="3"></a>Why isn&#8217;t the scheme called an &#8220;entitlement 
	card&#8221; any more?</h2>
  <p>The government made a tactical decision in 2003 to change the name. This 
	followed from constant criticism in the House of Lords, where many Peers refused 
	to use the word &#8220;entitlement&#8221;. This resulted by the end of 2002 
	in Lord&#8217;s Hansard dropping the description entirely and <a href="http://www.publications.parliament.uk/pa/ld200203/ldhansrd/vo021202/text/21202-01.htm">replacing 
	it with &#8220;Identity Cards&#8221;</a>. Opposition Peers claimed that the 
	Government was using fluffy language to disguise the true nature of the proposal. 
	The misnomer was also <a href="http://www.guardian.co.uk/guardianpolitics/story/0,3605,1204823,00.html">attacked 
	by Education Secretary Charles Clarke</a>. By the end of 2003 the Home Office 
	formally dropped all reference to &#8220;entitlement cards&#8221;. The central 
	purposes of the card system however have not changed.</p>
  <h2><a name="4"></a>What will the Identity Card be used for?</h2>
  <p>The Bill sets out a number of purposes for the Card and the Register. Some 
	are more open-ended than others. For example, the scheme is described as &#8220;a 
	convenient method for such individuals to prove registrable facts about themselves 
	to others&#8221;. The Bill also says that the card scheme will allow &#8220;the 
	provision of a secure and reliable method for registrable facts about such 
	individuals to be ascertained or verified wherever that is necessary in the 
	public interest.&#8221;</p>
  <p>&#8220;Public Interest&#8221; has a number of dimensions. Clause 1(4) of 
	the Bill defines it as being &#8220;in the interests of national security&#8221;, 
	&#8220;for the purposes of the prevention or detection of crime&#8221;, &#8220;for 
	the purposes of the enforcement of immigration controls&#8221;, &#8220;for 
	the purposes of the enforcement of prohibitions on unauthorised working or 
	employment&#8221; and &#8220;for the purpose of securing the efficient and 
	effective provision of public services.&#8221;</p>
  <p>On the face of it, this definition would imply that the card and the register 
	would be <a href="http://www.theregister.co.uk/2004/12/03/business_immigrant_checks/">necessary 
	to seek employment</a>, to <a href="http://quote.bloomberg.com/apps/news?pid=10000102&sid=adIU._FV1Wnw&refer=ukU.K">gain 
	access to health</a>, benefits and other services, and that it would be used 
	by police, security and immigration officers in the course of their functions. 
	However the words &#8220;for the purposes of the prevention or detection of 
	crime&#8221; could be connected to financial control and money laundering 
	regulations to provide a means by which the ID system can be used for an almost 
	limitless range of purposes. The could include operating a bank account, <a href="http://www.accountancyage.com/news/1138822">using 
	professional services</a> such as a solicitor or accountant, applying for 
	a permit or license, internal travel, buying property, stocks or shares, applying 
	for credit or using large amounts of cash.</p>
  <p>It is likely that the card and register may ultimately <a href="http://www.out-law.com/php/page.php?page_id=idcarddatabaseto1102340874&area=news">be 
	used to verify entitlement to most if not all public services</a> while the 
	Bill and the Regulatory Impact Assessment paves the way for widespread use 
	by the private sector. The Assessment states that the government will &#8220;work 
	closely with private sector organisations to ensure that the [ID card] scheme 
	develops along lines which will meet their business requirements&#8221;. This 
	means that links and transactions within private sector records are likely 
	to appear alongside the government-held registrable facts associated with 
	an individual.</p>
  <p><a href="http://www.vnunet.com/news/1159786">The Home Office recently stated</a>: 
	&#8220;We are proposing to make online checks against the register the norm, 
	except in those low risk/low value cases where a visual check is judged to 
	be sufficient&#8221;. <a href="http://www.parliament.the-stationery-office.co.uk/pa/cm200304/cmselect/cmhaff/uc130-vii/uc13002.htm">Responding 
	to a question</a> of whether libraries and video rental shops might require 
	the card the Home Secretary told the Home Affairs Committee: &#8220;Wherever 
	someone is required to prove their identity and those operating that particular 
	service have registered so they can use a reader then that would be fine&#8221;. 
  <h2><a name="5"></a>Who can gain access to the information on the national register?</h2>
  <p>Clause 19 of the Bill allows the disclosure of information from the register 
	without the individual&#8217;s consent to (among other agencies) police organisations, 
	the security services, Inland Revenue, the Department for Work & Pensions, 
	the Serious Organised Crime Agency and Customs & Excise.</p>
  <h2><a name="6"></a>What use can the police make of the card and the register?</h2>
  <p>Under clause 19 (3) of the Bill information from the register can be handed 
	to or accessed by police for purposes of prevention or detection of crime. 
	This provides substantial scope to use the information. Police may, for example, 
	apply to link fingerprint information on the register to &#8220;crime scene&#8221; 
	evidence. They must however establish that they have taken reasonable steps 
	to seek the information from other sources.</p>
  <h2><a name="7"></a>What personal information will be contained in the Register 
	and on the Card?</h2>
  
<p>The Government has continually asserted that the creation of the ID system 
  will result in the collection of less, not more, personal information than already 
  exists. In April, for example, the Home Secretary told BBC1's Breakfast programme: 
  &#8220;There will be no more information, in fact a lot less, and much less 
  accessibility than there are for shopping cards at the moment&#8221;. The Home 
  Secretary <a href="http://www.homeoffice.gov.uk/docs3/identitycards_041118speech.htm">repeated 
  this claim during a speech</a> in November, resulting in <a href="http://news.bbc.co.uk/1/hi/uk_politics/4018939.stm">a 
  robust response</a> from the retail sector.</p>
  <p>The government’s claim is deceptive in that it confuses data on the identity 
	card (as a chip embedded in a piece of plastic) with the national database, 
	which is where almost all the personal information will be held. (The Bill 

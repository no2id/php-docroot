<h1>The Corporate Agenda</h1>
<p>The Home Office has not come up with the ID scheme independantly.  The largest group lobbying for the proposed
plans are the very companies who will be selling equipment and systems to the government.</p>
<h2>Atos Origin</h2>
<p>Atos Origin is a multinational company with revenues of over €5 billion and 47,000 staff.  Atos Consulting is the third largest consulting firm in the UK, owing to its acquisition of giants such as KPMG and SchlumbergerSema.  Specialising in IT services Atos Origin's customers include household names such as Abbey National, United Biscuits and Coca-Cola as well as many government departments.  Atos Origin systems produce 1 in 10 payslips in the UK, and their chips appear on many credit cards.</p>

<!-- don't change anything after this -->
<p>They have been awarded the contract to run the Passport Office biometrics trial, 
  and are a likely contender for running the Home Office ID scheme, should it 
  come into force.</p>
<p>For a company like Atos Origin, a compulsory ID card scheme is a dream come 
  true &#8211; the entire population of a country will be forced to carry its 
  products. Such leverage is a huge opportunity, one which Atos Origin is likely 
  to take full advantage of.</p>
<p>In their document &#8220;A Smart Move is on the Cards&#8221; Atos give details 
  of some of their plans for an ID scheme which would have profound effects on 
  our society, in their words &#8220;the same transforming effect[&#8230;]as the 
  introduction of paper money had in the middle ages.&#8221;</p>
<p>Beyond the plans outlined by the Home Office, Atos Origin seeks to include 
  an e-purse on the ID card. An electronic purse on a compulsory card would become 
  the choice for small monetary transactions. This would increase Atos Origin&#8217;s 
  involvement in individuals&#8217; financial lives, complementing their credit 
  card and payroll interests.</p>
<p>Atos Origin would also become powerful players in the identity industry, ultimately 
  controlling how individuals identify themselves to others. As the key provider, 
  Atos Origin would be well placed to supply systems to businesses and public 
  services that want to use the ID system.</p>
<p>This is on top of being paid by the UK government for running the scheme itself. 
  &#8220;Over a period of 13 years, it is highly unlikely that the cost of a scheme 
  would exceed 13 x &pound;1.63 billion = &pound;21.9 billion.&#8221; Atos Origin 
  would be seeking a large slice of that, strengthening the value of its stock, 
  and enabling its grandiose ambitions.</p>
<p>For more details of Atos Origins ID plans, see their documents at <a href="http://www.atosorigin.co.uk/industries/publicsector/idcards/response.htm">www.atosorigin.co.uk/industries/publicsector/idcards/response.htm</a></p>

<h1>The cards </h1>
<p>The  government is planning to establish a system that will involve unprecedented  collation and monitoring of personal information. The House of Lords said the  Identity Cards Bill&rsquo;s name is misleading. </p>
<p>This is about more than plastic cards!</p>
<h2 id="what">What is an ID Card?</h2>
<p>The cards will be a  credit card-sized photo card and will include a computer chip. Although no examples of what the card will look like have been made available by the Home Office, they are likly to look something like existing driving licenses.</p>
<p>The biometric ID cards used in the recent Passport Office biometrics trial looked like this:</p>
<p align="center"><img src="/IDSchemes/FAQ/trialIdCard.jpg" alt="UK Passport Office Trial ID Card" width="200" height="128" /></p>
<p>Our artists impression of an ID card looks like this:</p>
<p align="center"><img src="/IDSchemes/FAQ/orwellMockCard.jpg" alt="An artists impression of what a National Identity Card might look like" width="200" height="128" /> </p>
<p>It has also been suggested that modified versions of passports and driving licenses will operate as identity cards, in addition to the cards themselves. </p>
<p align="center"><strong>The  ID card itself will be the part of the National Identity Register (NIR) you  will carry in your wallet.</strong></p>
<h2 id="howMuch">How much will an ID card cost me?</h2>
<p>No one knows for sure - well, perhaps the Home Office does, but they aren't telling.</p>
<p>Estimates of the cost-per-head of the system vary widely. There have been widely varying estimates of the cost to the individual, from the &pound;93 official Home Office quote to the &pound;300 of the LSE report.</p>
<p>In addition to this initial cost, the Bill requires payment to register any changes in the data held on the National Identity Register (NIR), for example a new address.</p>
<h2 id="whosEligable">Who is eligable for an ID card?</h2>
<p>This might also be seen as the question &quot;Who is <em>required</em> to register for an ID card?&quot;</p>
<p>All those over the age of 16 will be eligible for registration.</p>
<p>Anyone visiting the UK for more than three months will also be required to register for a card (although what they will need it for if they aren't residents here is unclear). </p>
<h2 id="forgery">How will the Government prevent forgery?</h2>
<p>The Government  has proposed using two forms of biometric data on the card:</p>
<ul>
  <li> iris scans,</li>
  <li>and  fingerprints</li>
</ul>
<p>These are proposed to  ensure the cards are unique to the individual, and would be used to verify if a card had already been issued to that person.</p>
<p> However, there is much doubt among experts as to the fallibility of both of  these types of data.</p><p>For full report see: <a href="http://is.lse.ac.uk/idcard/identityreport.pdf">http://is.lse.ac.uk/idcard/identityreport.pdf</a></p>

</p>
<h2 id="auditTrail">What is an audit trail? </h2>
<p>The audit trail is the record kept, by decree of the  Identity Cards Bill, of every occasion on which an individual&rsquo;s entry on the  NIR is accessed, for what reason and to whom the information has been passed.</p>

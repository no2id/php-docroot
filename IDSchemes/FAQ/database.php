<h1>The Database - The National Identity Register </h1>
<h2 id="what">What is the National Identity Register</h2>
<p>The National Identity Register (you'll often see this reduced to NIR) is a giant database used to hold information about people registered on the Identity scheme.</p>
<h2 id="theInfoStored">What information may be held on the  National Identity Register?</h2>
<p>The Identity Cards Bill proposes to hold <a href="/IDSchemes/faq.php#theInfo">51 different sorts of information on each individual</a>, this ranges from name, address and date of birth to a record of all the occasions on which your record has been accessed and by whom. The Bill leaves open the possibility of extending this list in the future through less scrutinised secondary legislation.</p>
<h2 id="accessToMyInfo">Will I be able to check what information is held about me on the National Identity Register (NIR)?</h2>
<p>The Home Office officially states that under the Data Protection Act 1998 an individual will be able to see the NIR information held on him/her subject to the procedures laid out in the legislation. </p>
<p>The Identity Cards Bill  would grant the Home Secretary the power to change information held on an  individual on the NIR if he believes it to be &lsquo;incorrect or inaccurate&rsquo; without  the consent of or informing the individual concerned. 

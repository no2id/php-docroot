<h1>Compulsion - being made to have a card</h1>
<p>New Labour ministers often talk about the imporance of choice. Will we be able to choose whether or not to have or carry ID cards? </p>
<h2 id="registration">Will it be compulsory to register for an ID card?</h2>
<p>As the  Identity Cards Bill stands, ID Cards will not be compulsory. However,  registration on the NIR will be much more pervasive.</p>
<p>Those signing up for  other documents, such as passports, will be automatically registered whether they like it or not.</p>
<p>The Identity Cardss Bill  leaves open the possibility that ID Cards could be made compulsory  at a later date. The Government has stated that this is their ultimate  intention.</p>
<p>Moreover,  the Home Secretary is granted the power in the Identity Cards Bill to order  certain individuals or groups to register for an ID Card. Once you are in a  category ordered to register, or you need a &quot;designated document&quot; to  live, you will have no choice.</p>
<h2 id="carrying">Will it be compulsory to carry a card?</h2>
<p>The current Identity Cards Bill does not make it compulsory to carry the card. </p>
<p>This will certainly make the Cards less effective in meeting certain stated aims. It is likely that compulsory carrying of cards will be introduced at a later date. Indeed, this is likely to occur in practice because of the number of occasions on which the card may have to be produced.  <!-- don't change anything after this -->
</p>

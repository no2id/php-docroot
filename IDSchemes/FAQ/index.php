<dt><a name="1"></a>What is an ID card?</dt>

<dd>
<p>The government is planning to establish a system that will involve
unprecedented collation and monitoring of personal information. The House of
Lords said the Identity Cards Bills name is misleading.</p>

<p>This is about more than plastic cards!</p>

<p>An ID card, as part of the National Identity Scheme, will impose upon every
adult legally resident in this country, including foreign nationals, a card
which will link to the National Identity Register, holding fifty pieces of
information about each of them.  The ID card itself will be the part of the
National Identity Register (NIR) you will carry in your wallet.</p>

<p>This technology brings many pitfalls, including enormous cost, a one stop shop for organised criminals and intrusion into civil liberties.
(<a href="../whyNot.php">More about the pitfalls of the scheme.</a>)</p>
</dd>


<dt><a name="2"></a>Why introduce a National Identity Scheme?</dt>

<dd>
<p>The Government claims its decision to introduce its identity scheme is based
in part on the fact that many countries are starting to put biometrics into
their passports. The decision to introduce biometrics into some existing
identity documents has therefore already been made, and the majority of the
population would soon be using biometric identity documents (in the form of
passports).</p>

<p>But this is being used as an excuse for a National Identity Register (NIR),
masked by the concept of an ID card. The NIR will store
<a href="http://www.opsi.gov.uk/ACTS/acts2006/60015--b.htm#sch1">fifty
categories of information about you</a>, and can be changed or added to without
your consent.</p>

<p>The Government has suggested that the Identity Cards Act 2006 will tackle a
number of problems, such as <a href="../whyNot.php#terrorism">terrorism</a>,
<a href="../whyNot.php#identityfraud">identity theft</a>,
<a href="../whyNot.php#illegalimmigration">illegal immigration</a>, and
<a href="../whyNot.php#benefitfraud">benefit fraud</a>. It has never explained
how. NO2ID says an identity register and ID cards are mostly irrelevant to
these problems, but they will restrict freedom, privacy, and civil rights for
everyone.</p>

<p>Tony McNulty, the Home Office minister once responsible for identity cards
said: "Perhaps in the past the government, in its enthusiasm, oversold the
advantages of identity cards. We did suggest, or at least implied, that they
might well be a panacea for identity fraud, for benefit fraud, terrorism,
entitlement and access to public services. In its enthusiasm, the government
had over-emphasised the benefits to the state rather than for the individual in
providing a gold standard in proving your identity."</p>
</dd>


<dt><a name="3"></a>Who will be eligible to have an ID card?</dt>

<dd>
<p>This is the same question as "Who is required to <em>register</em> for an ID
card?"</p>

<p>Everyone over the age of 16 will be eligible for registration.</p>

<p>Anyone visiting the UK for more than three months will also have to
register. (Though what non-residents will have to do with an ID card is
unclear.)</p>
</dd>


<dt><a name="4"></a>What information may be held by the National Identity Scheme?</dt>

<dd>
<p>The Identity Cards Act sets out
<a href="http://www.opsi.gov.uk/ACTS/acts2006/60015--b.htm#sch1">fifty
different sorts of information to be held on each individual</a>, these range
from name, address and date of birth to a record of all the occasions on which
your record has been accessed and by whom.  The Act makes it possible to extend
this list in the future, by Home Office regulations.</p>
</dd>

<dt><a name="5"></a>Will I be able to check the information held about me?</dt>

<dd>
<p>You will be able to check it, but not to change it if it is wrong.  You are
however personally responsible for the accuracy of the information!</p>

<p>The Home Office states that the Data Protection Act 1998 will apply to the
NIR, so an individual will be able to see the NIR information held on him/her.
But the Data Protection Act has numerous
<a href="http://www.opsi.gov.uk/ACTS/acts1998/80029--d.htm#27">exceptions for
information used by government</a>.</p>

<p>The Home Secretary has the power to change information held on a persons
record if he believes it to be inaccurate. He need not ask or tell the
individual concerned.</p>
</dd>


<dt><a name="6"></a>When will the first cards be issued?</dt>

<dd>
<p>The <a href="http://www.identitycards.gov.uk/">UK IPS (Identity and Passport
Service)</a> says the first ID cards will be issued from 2008/09.</p>
</dd>


<dt><a name="7"></a>What are biometrics?</dt>

<dd>
<p>The Home Office says that:</p>

<blockquote>"A biometric is a unique identifying physical characteristic.
Examples include facial recognition, iris patterns and
fingerprints."</blockquote>

<p>Biometrics is not simple. Biometrics is the science of measuring and
statistically analyzing human body characteristics, such as faces, iris
patterns, fingerprints, voice recognition and so on. Features of them <em>are
not</em> always unique, and so biometrics works with the statistical
probability rather than offering definite identification.</p>

<p>The idea is an added "safeguard" to prevent another person from using your
ID card. A fingerprint is much more difficult to forge than a signature. But
that relies on biometric data being checked every time the card is used. Each
check against the national database will be recorded.</p>
</dd>


<dt><a name="8"></a>What biometrics will be used?</dt>

<dd>
<p>This is not yet certain. The Act allows the recording of <em>any</em>
biometric. It is likely that facial image, fingerprints and iris patterns will
be used. Other biometrics include retina scans, voice patterns, hand
measurements and even ear shape.</p>
</dd>


<dt><a name="9"></a>How reliable are they?</dt>

<dd>
<p>Not reliable enough! The government's own tests on a sample of 10,000
volunteers, including more than 1,000 disabled volunteers, showed that:</p>
<ul>
    <li>Facial scans worked for 69% of the "quota" group and 48% of disabled
    volunteers. That is three in ten of the quota group and more than half of
    the disabled who were unable to use the facial scan technology.</li>
    <li>Fingerprints worked for 81% of "quota" group and 80% of disabled
    people, but was more successful for young people. Two in ten in both groups
    failed.</li>
    <li>Iris scans worked for 96% of "quota" volunteers and 91% of the disabled
    group. Some glasses wearers failed the checks unless they took their
    glasses off.</li>
    <li>0.62% of the disabled group could not enrol using any of the systems.
    There are 8.6 million registered disabled people in the UK. So if the
    volunteers are representative, at least 53,000 will not be able to give a
    usable biometric.</li>
    <li>Black participants and participants aged over 59 had lower iris
    enrolment success rates according to the study.</li>
</ul>
<p>These statistics came from
<a href="http://news.bbc.co.uk/1/hi/uk_politics/4580447.stm">this BBC News
article</a>.</p>
</dd>


<dt><a name="10"></a>Will it be compulsory to have an ID card?</dt>

<dd>
<p>Yes.</p>
</dd>


<dt><a name="11"></a>Will it be compulsory to carry a card?</dt>

<dd>
<p>Probably.  When these FAQs were first written, six months ago, the Home
Office maintained that introduction would be voluntary. That was twisting the
truth. At some stage it is likely that it will be compulsory to carry one,
although this is not yet law.</p>

<p>In practice, once you need an ID card to live, you will have no choice.</p>
</dd>


<dt><a name="12"></a>Will my information be given out without my consent?</dt>

<dd>
<p>Yes, the Identity Cards Act sets out a number of different departments to
whom the NIR will be available, including Revenue and Customs and all the
intelligence and police agencies. The Home Secretary can authorise most of the
information to be given to any government body if it is needed for any
government purpose.</p>
</dd>

</dl>


<h2>Briefings on other Bills</h2>
<dl> 
  <dt><a name="seriouscrimebill" href="NO2IDSeriousCrimeBillBriefingFEB2007.pdf">Briefing on
  Serious Crime Bill for Lords Second Reading, February 2007</a></dt>
  <dd>Summary briefing, focussed on the broad data-sharing and
  data-mining/matching aspects of the Bill. This is <em>clearly</em> an ID
  scheme-enabling Bill, which includes such horrors as the effective abolition
  of data protection in respect of certain 'official uses' of data.</dd> 
  
  <dt><a name="bordersbill-feb-2007" href="NO2IDUKBordersBillBriefingFEB2007.pdf">Briefing on UK
  Borders Bill for Commons Second Reading, February 2007</a></dt> <dd>Summary
  briefing on what is quite evidently another ID scheme and data-sharing Bill.
  Specific concerns include: weakened rule of law and increased official
  discretion; damage to privacy and confidentiality; economic and reputational
  damage to the UK; and the deliberate hiding of ID scheme costs in other
  departments' budgets.</dd>

  <dt><a name="immigcitbill-oct-2008" href="NO2IDsubmissionToJCHRonDraftIACBoct08.pdf">Briefing
  on Draft (Partial) Immigration and Citizenship Bill for JCHR, October
  2008</a></dt> <dd>Submission to the Joint Committee on Human Rights on a
  partial Bill that leaves out clauses on data-sharing and biometrics, but
  which is drafted so broadly as to affect ALL residents of the UK, including
  British citizens. Specific concerns include: effective compulsion to carry
  official ID (ID card or passport); compulsory reporting of all hotel
  registrations; requirement to provide official ID in order to gain
  employment.</dd>

  <dt><a name="coronersjusticebill-jan-2009" href="2009-01-22-coroners-and-justice-bill-briefing.pdf">Briefing 
  on Coroners and Justice Bill for Commons Second Reading, January 2009</a></dt> 
  <dd>Summary briefing on data sharing
  clauses in a Bill ostensibly about reform of the inquest system. Of
  overriding concern is the creation of a fast-track regulatory procedure to
  sweep away data protection, human-rights considerations, confidentiality,
  legal privilege, and <em>ultra vires</em> when they would stand in the way of
  any use, acquisition or dissemination of information in pursuit of
  departmental policy.</dd>
  
  <dt><a name="iddocsbill-june-2010" href="2010-06-08-identity-documents-bill-briefing.pdf">Briefing 
  on Identity Documents Bill for Commons Second Reading, June 2010</a></dt> 
  <dd>Summary briefing on the Bill intended to repeal the Identity
  Cards Act 2006. The Bill as originally drafted actually broadens
  some of the already over-broad offences created by the 2006 Act and
  reintroduces some of the deeply flawed official conceptions of
  'identity' inherent in the National Identity Scheme. The Bill does
  nothing to address 'ID cards for foreign nationals' &#8212; actually
  Biometric Residence permits &#8212; which are issued under the UK
  Borders Act 2007.</dd>
  
</dl>


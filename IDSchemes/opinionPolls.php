<h2 id="evaluating">A guide to evaluating opinion poll results</h2>
<p><a href="#top">Back to top </a></p>
<p>Opinion polls offer a snap shot of public opinion at a specific point in time.
  To be credible, they need to cover the right sample, ask the right questions
and get the figures right!</p>
<p>To interpret the results correctly, we need to understand who commissioned the
  work, who carried out the research using what methodology and sample, what else
was happening during the fieldwork dates and how biased the questions might be.</p>
<h3>Who commissioned the Poll?</h3>
<p>Who commissioned the research will influence what questions are asked and
  how they are asked, with a potentially significant impact on the results. Every
  organisation commissioning a poll has an agenda, and a question they want to
  answer. Even in polls which have been well-designed and carried out by a reputable
  polling house, a bias is likely to be inbuilt, whether deliberate or not! It
  is therefore important to understand what question is being asked, and why.</p>
<h3>Who carried out the research?</h3>
<p>A valid opinion poll needs to be carried out by a reputable polling house.
  Most pollsters are members of professional organisations which aim to maintain
  standards. </p>
<p>These include people like Association of Professional Opinion Polling Organisations
  and the British Polling Council. Member include MORI, NOP, Gallup, Harris,
  ICM, and internet pollster YouGov.</p>
<h3>What methodology was used?</h3>
<p>Face to face, telephone and internet polls will all generate different results,
  and it is difficult to compare them to each other even if the questions are
  identical. Issues facing each methodology include the reliance upon a professional
  fieldforce for face-to-face interviewing, and the fact that respondents tend
  to give fewer negative responses when face-to-face. Telephone interviewing
  is beginning to face complications due to the increasing use of mobile phones,
  which makes it harder to obtain a representative sample, while internet polling
  also struggles to provide a representative sample, although this is overcome
  as ever more households connect to the internet.</p>
<p>Telephone phone-ins where respondents phone into TV or radio shows or respond
  to ads in the paper, are not true opinion polls. This is because respondents
  are self-selected and do not offer a representative sample of the population.
  Results from phone-ins should be used with caution.</p>
<h3>What was the sample?</h3>
<p>Reputable polling houses consider 1000 respondents to be the minimum sample
  size for robustness. </p>
<p>In addition to sample size, the sample needs to be demographically representative
  of the total UK population, or representative of a specific subsection that
  the poll is aiming to get opinions from, eg ethnic minorities or a disabled
  group.</p>
<p>In addition to being a demographic match (ie representative of gender, age,
  ethnicity, religion, region, class, lifestage), a robust poll should also be
  attitudinally representative. For example, it is possible that certain types
  of people are less likely to want to take part in polls, particularly on controversial
  issues or when they feel they might hold a minority view. </p>
<h3>What else was happening during the research period?</h3>
<p>Most people don't hold deep-seated opinions on every single issue, particularly
  new issues or those which they feel don't concern them. Because opinion polls
  are a snapshot in time, this means that the results can be swayed by topical
  news or events which took place at the same time. </p>
<p>For example, asking questions about terrorism directly after well-publicised
  terror attacks will generate different responses than when asked during a more
  peaceful period.</p>
<h3>Could the questionnaire design be having an impact? </h3>
<p>Questionnaires need to be short, simple and unambiguous, They should avoid
  jargon and shorthand, as well as uncommon or sophisticated words. Importantly,
  they should avoid introducing bias into the responses.</p>
<p>Bias can be introduced in a number of ways. For example, an openly leading
  question or a question that is structured in such a way that a particular response
  appears obvious. âdo you agree...â will frequently preface a leading question,
  as would any particularly positive, negative or emotive terminology,</p>
<p>Other ways of introducing bias include a certain presumption about the subject
  being researched. For example, asking respondents how many books they've read
  in the past year, presupposes that all the respondents read books. It would
  be more appropriate to ask whether they read, first.</p>
<p>Pre-coded responses also tend to introduce bias. The options available will
  rarely cover all the possible responses, and often reflect the researcher's
  opinion of what all the possible answers and opinions might be. Respondents
  often find it frustrating to be unable to express views in their own words,
  and even where the option exits for additional open answers, respondents struggle
  to find their own responses, once their thinking has been led by pre-coding.</p>
<p>Lists of pre-coded responses also tend to introduce bias in other ways, with
  several studies showing that the location of different responses within a list
  having an impact on the outcome. Put simply, if what is considered the most
  popular response is put at the beginning of a pre-coded list, it is likely
  to become so.</p>
<h3>Interpreting the results</h3>
<p>Even a well conducted poll can be misrepresented! To be accurately interpreted,
  analysis of the results of a poll needs to take everything into account â who
  commissioned it, carried it out, on what sample, during what time period, using
  which methodology and for what purpose?</p>
<p>On top of this, it is important to remember that polls and questionnaires
  are much better at telling us what rather than why. It is very difficult to
  uncover respondents' deep-rooted opinions, feelings and motivations through
  a set questionnaire â more in-depth qualitative research is needed for that,
  particularly for sensitive, emotive or embarrassing topics.</p>
<p>Respondents also struggle to explore or give opinions on unfamiliar concepts.
  For example if they are being asked about something new which is difficult
  to understand, or which doesn't exist yet, their opinions tend to be less well-formed
  as it is difficult to imagine how it might impact their lives. This also means
  that they are more likely to be influenced by media reports around the topic,
  or even by questionnaire design.</p>
<p>Finally, sometimes, what respondents don't say is just as important as what
  they do say. Non-response rates tend to be very important around intimate or
  sensitive topics. Despite the best efforts of sample design, non-respondents
  may be different to respondents in some significant (usually but not always
  attitudinal) way. Unfortunately it is very difficult to get accurate demographic
  or attitudinal information on non-respondents, and therefore any impact is
  often surmised, rather than proven. One notable example does exist, however.
  Political opinion polls covering the 1992 election in the UK failed to accurately
  predict the election result. According to an MRS inquiry, this was due to a
  number of reasons including a late swing in votes, and sampling inadequacies.
  Significantly, the inquiry also found that the poll results were biased because
  Conservative voters were less likely to reveal their loyalties due to an unwillingness
  to answer certain questions and possibly, due to a refusal to take part in
  polls at all.</p>
<h2 id="shifting">Shifting public opinion</h2>
<p><a href="#top">Back to top </a></p>
<p>Support for identity cards has fallen from a high of 85% after 9/11 to 45%
  in June 2005, and around 52% in February 2006 once the ID Cards Bill was passed.
  Not all the polls are comparable, and some are less robust than others. However,
  the organisations MORI and YouGov both agree that these changes represent shifting
  public opinion against id cards, and are not merely the result of differing
methodologies and samples.</p>
<p>The polls carried out in September 2003 and June 2005 by YouGov for the Daily
  Telegraph are the clearest indicator that public opinion is turning against
  id cards. Both polls have the same question wording, same sample design and
  similar preceding questions. However, in September 2003 74% of respondents
  supported the scheme, whereas by June 2005 this has dropped to 45%. Interestingly,
  there were 7% âdon't know' responses in 2003, and 13% in 2005, indicating an
  increasing number of people who feel they don't have enough information to
  form an opinion.</p>
<p>In the June 2005 poll, 84% of respondents are worried about the potential
  disruption and inconvenience of implementation, while 66% believe that the
  government should not be spending Â£6 billion on the scheme (this increases
  to 81% against when the estimated costs rise to Â£10-19 billion).</p>
<p>Even in September 2003, where support for id cards was high, respondents were
  unsure of the supposed benefits, with 82% agreeing that it would cut down on
  benefit fraud, but 66% believing that the cards could be forged. Respondents
  were also clear about where the limits of the card should be, with 86% stating
  it should be free of charge; 72% stating they did not want data-sharing between
  government departments; and 65% stating it should only establish identity and
  not carry biometric or home address details. </p>
<p>NO2ID/ICM polls are also broadly comparable with the same question asked in
  June 2005, November 2005, and February 2006. The results show a decline in
  support between June and November 05. In June, 55% supported ID cards compared
  to 50% in November. In addition, the number of people feeling strongly about
  the issue and agreeing that id cards are âa very bad ideaâ, increased from
  18% in June to 23% in November.</p>
<p>As the true details of the proposed identity card scheme and national identity
  register have become clearer, public support has fallen.

<h1>What is the database state?</h1>
<p>The 'database state' is what we call the tendency to try to use computers
   to manage society by watching people. There are many interlocking government 
   plans that do this. Together they mean officials poking into YOUR private 
   life more than ever before.</p>

<p>All the databases could be linked to, or indexed by, the 
   <a href="/IDSchemes/whyNot.php"><strong>National Identity Register
   </strong></a> (NIR) that is the main aim of the 'ID cards' scheme. Your 
   NIR number would be the key to your whole life. And by "information sharing", 
   what you tell one public servant could be passed to anyone. The government 
   name: "Transformational Government" sounds nicer &#8212; until you understand 
   what is being transformed is not government but its power over you.</p>

<p>Currently the planned systems include:</p>

<ul>	
  <li><a href="https://www.no2id.net/get-involved/idCentres">
      <strong>ID interrogation centres</strong></a>, for passports and ID cards</li>

  <li><a href="http://www.renewforfreedom.net">
      <strong>ePassports</strong></a> that help collect data about your travel for...</li>

  <li>International <a href="http://www.ind.homeoffice.gov.uk/aboutus/eborders/faqseborders">
      <strong>eBorders schemes</strong></a> that exchange <a href="http://www.out-law.com/page-8333">
      <strong>Passenger Name Record information</strong></a> with foreign countries 
      as well as collecting them</li>

  <li>Recording of <em>all</em> car journeys, using 
      <a href="http://www.openrightsgroup.org/orgwiki/index.php/National_Vehicle_Tracking_Database">
      <strong>Automatic Number Plate Recognition (ANPR)</strong></a></li>

  <li><a href="http://www.journalonline.co.uk/article/1002706.aspx"><strong>
      'Entitlement cards'</strong></a> as part of, or linked to the ID scheme, 
      logging use of public services</li>

  <li><a href="http://www.thebigoptout.org/?page_id=3"><strong>Centralised 
      medical records</strong></a> without privacy</li>

  <li><a href="http://www.leavethemkidsalone.com"><strong>Biometrics in schools
      </strong></a> &#8212; fingerprinting children as young as 4 or 5</li>

  <li><a href="http://www.arch-ed.org/issues/databases/contactpoint.htm">

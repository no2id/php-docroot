<p><a href="/resources/motions/">Councils and public bodies across the
UK have passed motions against ID cards.</a> A number of NO2ID local
groups have successfully lobbied local councillors and union
representatives to get them to do this.</p>

<p>Join the e-mail list for your local group (look below for the "Mailing List:" 
  link) to find out about activities and the campaign in your area. In areas with 
  just a mailing list, you may find other people willing to start a group with 
  you. We're eager to hear from anyone who'd like to volunteer.</p>


<h1>NO2ID Local Groups</h1>

<p>Opposition to the database state exists everywhere in the UK. Groups are gathering 
  together in towns and cities under the NO2ID banner to campaign against this 
  dangerous assault on our privacy and freedoms. To get more involved, please 
  contact your nearest group. If there isn't one near you, consider starting one! 
  If you can't make contact with your nearest group or would like to start a group 
  of your own, please contact our local groups coordinator &#8212; <a
href="mailto:local.groups@no2id.net">James Elsdon-Baker</a>.</p>


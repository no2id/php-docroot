<h1>NO2ID campaigns in Home Secretary's heartland of Norwich </h1>
<p>The NO2ID Voter Education Tour began on Saturday in the streets of Norwich,
		delivering many thousands of leaflets to voters in the city centre and in Charles
		Clarke's Norwich South constituency.</p>
<p>Joined by prospective Parliamentary candidates from the Green, Liberal Democrat
		and UKIP parties, and supporters from as far as Cambridge and Liverpool, NO2ID
		braved an uncharacteristic Norfolk blizzard to advance door-to-door through
		Norwich.</p>
<p>The facts about ID cards were explained to residents as they were made aware
		just how little their MP cared for the privacy, freedoms and principles of
		the nation, and what ID cards would cost the taxpayer, and them personally,
if they were to be introduced.</p>
<p>There will be ongoing leafleting and campaigning in Norwich in the run up
		to the election, with public meetings planned and support from the city's students
		at UEA.</p>
<p>As the first in a series of trips to marginal constituencies whose sitting
		MPs support ID cards, through the rise or fall of the Home Secretary Norwich
		will play a symbolic part in the campaign against ID cards.</p>
<p>Andy Robson, campaigns manager of NO2ID, said: &quot;Having seen the complete
		rejection of ID cards demonstrated by the city council, the MP for Norwich
		North Ian Gibson, and thousands of Norwich people on Saturday, I'd suggest
Mr. Clarke change his tune or face ejection at the election.&quot;</p>

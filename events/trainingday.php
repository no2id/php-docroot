<h1>NO2ID training day &shy; helping you to fight for your rights!</h1>
<p>Saturday 12<SUP>th</SUP> saw the first NO2ID training day bring important
		skills to local group activists from around the country, whose work is key
		to taking the dangers of ID cards and the identity register to the public in
		towns and cities across the UK.<BR>
&nbsp;<BR>
Phil Booth, national coordinator of NO2ID, began the day by talking about how
far NO2ID had come in under a year, from rumblings of discontent across a broad
group of civil liberties and privacy organisations to a fully fledged national
campaign with tens of thousands of members across the UK.<BR>
&nbsp;<BR>
Guy Taylor, local group coordinator, spoke about starting, running and keeping
a local group going. Guy is an experienced issues campaigner, having notched
up many years of tireless flyering in wet and windy shopping centres.<BR>
&nbsp;<BR>
Motivation is essential to any campaign run by volunteers, and Tim Maugherman,
director of TEAM Communications, discussed what drives people to join a campaign
and how to keep them involved and happy.<BR>
&nbsp;<BR>
After lunch, the serious business of lobbying MPs and politicians was tackled
in earnest with the aid of Neil Gerrard, Labour MP for Walthamstow and long time
supporter of NO2ID. Talking frankly about how to and how not to go about making
an impression upon your representative, Neil guided the would-be lobbyists through
some of the pitfalls that can stop a campaign from gaining traction.<BR>
&nbsp;<BR>
The day was rounded off with a workshop led by Andy Robson, NO2ID head of campaigns,
highlighting the importance of communications: clearly communicating NO2ID&rsquo;s
message, keeping in touch with the press, and original NO2ID national coordinator
Mark Littlewood spoke on attracting the attention of the broadcast media. As
one attendee reported, asking those present to practice articulating one point
of the campaign message "helped to clarify issues and raise confidence".<BR>
&nbsp;<BR>
With a good turnout from all over the south-east, south-west and home counties,
all attendees said that the event had been helpful, and would be interested in
future events.<BR>
&nbsp;<BR>
"I found all of the workshops useful and each for different reasons.&nbsp; I
thought the mix was spot on&rdquo;, said one attendee, &ldquo;the day was well
thought out, and the workshops were all complementary.&nbsp; It was good to meet
other activists."</p>

<h1>NO2ID mourn imminent loss of privacy and freedom that Churchill fought for
53 years ago</h1>
<p style="text-align:center"><img src="/images/photos/idabolitionday.jpg" alt="NO2ID Celebrate Identity Card abolition day" width="400" height="319"></p>
<p>Under the watchful gaze of Winston Churchill, NO2ID held an hours' vigil today
		(Monday) in a corner of Parliament Square that is stands for liberty, privacy
		and freedom from the powers of the state.</p>
<p>In funereal black, <B>NO2ID </B>and <B>Liberty </B>mourned the short memory
		and lack of sense that leads this government to reintroduce ID cards and the
		National Identity Register, 53 years after Churchill scrapped the compulsory
		ID card system brought in during the war.</p>
<p> <img src="/images/photos/eyeballcop.jpg" alt="The all seeing eyes of the database state" width="100" height="271" style="float:right;margin:5px;">The proceedings were watched with interest by the authorities, including
		Charing Cross constabulary and the all-seeing eyes of our database state
		- costumes provided by Liberty depicting giant, unblinking eyes, watching me,
		watching you. The group was moved on after an hour and a half, with police
		claiming the polystyrene breastplates of the costumes, not unlike police
		body armour, <B>looked like explosives</B>.</p>
<p>After all that Churchill and the servicemen of the Second World War did to
		save us from oppression by authoritarian regimes, the Government seems intent
		on introducing <B>what the Bow Group describe as "the preserve of fascist and
		communist states"</B> - ID cards, police powers of stop, scan and swab, and
arrest without trial.</p>
<p> Phil Booth, national coordinator for NO2ID, said: </p>
<blockquote>
		<p>"It is a terrible thing to see the government rushing to recreate mistakes from
				the past, especially when the price that will be paid is so dear - intrusion
				by the state, loss of privacy and a means to institutionalise racial prejudice
				within Britain's multicultural society. And that's not to mention the &pound;100
		cost for each adult over 16 and over &pound;5.5bn of taxpayers money.</p>
		<p>"Churchill must turn in his grave to see this after all he had done. If
				he were here now, he would urge politicians of all parties who truly
				support freedom to join NO2ID and campaign against them as he did in
				1951, under the banner: "Set
		the people free!"</p>
</blockquote>

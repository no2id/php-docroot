<p style="text-indent: 2.0em;"><em>"No one should fear correct identification." &#8212; David Blunkett</em></p>

<br />

<p>Fans of ID cards often say, if you've done nothing wrong you have nothing to fear. 
   They are na&#239;ve.</p>
	
<p>There are lots of good reasons you might want to hide. You may be a woman 
   seeking safety from a former partner <a href="#footnote1">[1]</a>. You 
   might be fleeing more general family or community persecution 
   <a href="#footnote1">[2]</a>. You might be a political refugee from 
   an overseas government. You might be a witness to crime 
   <a href="#footnote1">[3]</a>. You might be someone who has been forced 
   to work for criminals. You might be a former criminal attempting to 
   leave a gang.</p>
 
<p>The ID scheme will make this much much harder &#8212; perhaps impossible, 
   if you do not have money and help. All such people are potentially in danger. 
   As are undercover journalists and police officers who are pretending to be 
   someone else in the public interest.</p>

<p>The Home Office makes much of the idea that biometrics &#8212; fingerprints 
   and facial images &#8212; stored on the ID card system will "lock" 
   individuals to a single identity. The government is enthusiastic about 
   checking people against databases of education and employment history 
   or police (not necessarily criminal) records, in the name of security.</p>

<p>It does not appear to care much that for many people privacy 
   <strong>is</strong> security.</p><br />

<p>&#8211;&#8211;</p>

<p><a name="footnote1" id="footnote1">1)</a> There are more than 400 organisations 
   in England alone providing women's refuge services. There are generally 
   800-900 homicides a year in Britain, three quarters of the women and 
   half of the men being killed by someone known to them: 
   <a href="http://www.parliament.uk/COMMONS/lib/research/rp99/rp99-056.pdf">
   http://www.parliament.uk/COMMONS/lib/research/rp99/rp99-056.pdf</a></p>
   
<p><a name="footnote2" id="footnote2">2)</a> Recent controversy has focussed 
   on 'honour crime' and forced marriages, but personal vendettas also spring
   from other reasons.</p>
   
<p><a name="footnote3" id="footnote3">3)</a> There are hundreds of so-called 
   'protected witnesses' for whom either a continuous personal file or one 
   that suddenly starts mid-life would pose a problem.</p>


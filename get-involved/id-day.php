<h1>ID-Day, Monday 26th March 2007</h1>
<br />    
<p>On ID-Day and in the days leading up to it, NO2ID groups will be holding actions 
  and events all over the UK. Press releases and messages from local groups are 
  already beginning to go out but even if you don't have an event organised near 
  you, you can still get involved.</p>
<p>Our request is simple: we want you to send us a digital photo of your face.</p>
<p>Thousands of you pledged not to comply with ID cards, many tens of thousands 
  have renewed their passports early and hundreds of thousands are opting out 
  of NHS 'spine' via <a href="http://www.TheBigOptOut.org">TheBigOptOut.org</a> 
  and leaflets in GP surgeries.</p>
<p>You are far from alone. Yours is a face in the rapidly-growing crowd of people 
  who reject government ID control and the database state. Together we can create 
  a living, graphic representation of that crowd.</p>
<p>Join with us and millions more who refuse to be treated like criminal suspects, 
  who don't want their kids fingerprinted without consent, who want to keep their 
  medical records between themselves and their doctor, who don't trust the government 
  not to misuse, sell or allow their personal information to be hacked...</p>
  
<h2>Show them you're not a number...</h2>

<p>...by sending a photo of your face from your camera phone, or [when the service 
  goes live in the next few days] uploading a photo via the form below. N.B.
  the lines will stay open for one month following ID-Day, so you can 
  still send us your picture.</p>
<p>To send an MMS message:</p>
<p>1) Take a photo of your face (or of anyone who wants to join in) with your camera phone, and choose 'Send'.</p>
<p>2) VERY IMPORTANT: add the word <strong>no2id</strong> to the body text of 
  the message or &#8211; if your phone allows you to &#8211; in the 'Subject' line of the 
  message.</p>
<p>3) Send the message to <strong>60300</strong></p>
<p>4) If successful, you will receive a message back saying: &quot;Thank you for 
  supporting ID-Day. You have been charged 25p.&quot; This is NOT a fund-raising 
  exercise, so we have kept the call charge to an absolute minimum.</p>
<p>The first public display of faces will be in the media on ID-Day itself and 
  at events across the UK. You can check out <a href="http://forum.no2id.net/viewforum.php?f=4">details 
  of local events in our forums</a>.</p>

